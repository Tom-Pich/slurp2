<article>
	<h2>Détails de paroi et de roche</h2>
	<p>Le grain du terrain, les anfractuosités, ce qu'on touche et ce qui blesse.</p>

	<details class="liste">
		<summary>Anfractuosité</summary>
		<p>Creux irrégulier, crevasse ou renfoncement dans la roche. Cachette naturelle, prise pour les doigts.</p>
	</details>

	<details class="liste">
		<summary>Aven</summary>
		<p>Puits naturel qui s'ouvre verticalement dans la roche calcaire. Bouche béante dans le sol, profondeur inconnue.</p>
	</details>

	<details class="liste">
		<summary>Gouffre / Abîme</summary>
		<p>Ouverture béante d'une profondeur vertigineuse. Le vide vers le bas, celui qui attire et terrifie.</p>
	</details>

	<details class="liste">
		<summary>Doline</summary>
		<p>Dépression circulaire en terrain calcaire, douce en surface, souvent reliée à des cavités souterraines.</p>
	</details>

	<details class="liste">
		<summary>Marmite de géant</summary>
		<p>Cuvette parfaitement circulaire creusée dans la roche par les tourbillons d'un cours d'eau. Forme parfaite et inquiétante.</p>
	</details>

	<details class="liste">
		<summary>Lapiaz</summary>
		<p>Surface calcaire striée, creusée, découpée en lames et en rainures par l'eau. Terrain blessant, sonore, lunaire.</p>
	</details>

	<details class="liste">
		<summary>Karst</summary>
		<p>Paysage calcaire troué, creusé de dolines, d'avens et de galeries souterraines. Un relief qui cache autant qu'il montre.</p>
	</details>
</article>