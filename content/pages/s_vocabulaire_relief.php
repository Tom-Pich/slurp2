<article>
	<h2>Vue immersive</h2>
	<p>Ce qu'on perçoit depuis l'intérieur du relief – Les détails du terrain qui s'imposent quand on est dedans : obstacles, appuis, passages.</p>

	<details class="liste">
		<summary>Versant</summary>
		<p>Flanc d'une montagne ou d'une colline, vu dans son ensemble. Ce que l'on monte ou descend.</p>
	</details>

	<details class="liste">
		<summary>Pente</summary>
		<p>Inclinaison du terrain. Peut être douce ou impitoyable.</p>
	</details>

	<details class="liste">
		<summary>Replat</summary>
		<p>Portion de terrain presque horizontale qui interrompt une pente. Une pause dans la déclivité — naturelle, inattendue. Point d'appui, d'abri ou de rassemblement selon sa taille.</p>
	</details>

	<details class="liste">
		<summary>Adret / Ubac</summary>
		<p>L'adret est le versant exposé au soleil — chaud, sec, ouvert. L'ubac est son envers dans l'ombre — humide, obscur, hostile. Le même relief, deux mondes opposés.</p>
	</details>

	<details class="liste">
		<summary>Épaulement</summary>
		<p>Palier intermédiaire sur un versant, comme un replat qui marque une pause dans la pente.</p>
	</details>

	<details class="liste">
		<summary>Terrasse</summary>
		<p>Replat plus marqué sur un versant, parfois en série. Point de respiration, de rassemblement.</p>
	</details>

	<details class="liste">
		<summary>Balcon</summary>
		<p>Replat en hauteur qui ouvre sur une vue plongeante. Lieu de surplomb, d'observation — ou d'embuscade.</p>
	</details>

	<details class="liste">
		<summary>Vire</summary>
		<p>Étroit replat horizontal sur une paroi ou un versant très raide. Un seul pied de large, parfois. Le vide d'un côté.</p>
	</details>

	<details class="liste">
		<summary>Corniche</summary>
		<p>Avancée rocheuse en surplomb sur une pente. Peut servir d'abri ou constituer un danger de chute.</p>
	</details>

	<details class="liste">
		<summary>Saillie</summary>
		<p>Excroissance de roche ou de terrain qui dépasse de la surface — poignée naturelle, obstacle, accroche du regard.</p>
	</details>

	<details class="liste">
		<summary>Talus</summary>
		<p>Pente régulière de matériaux accumulés au pied d'un escarpement ou d'une paroi. Terrain instable, sonore sous les pas.</p>
	</details>

	<details class="liste">
		<summary>Éboulis</summary>
		<p>Amoncellement de blocs et de pierres détachés d'une paroi. Terrain difficile, bruyant, potentiellement instable.</p>
	</details>

	<details class="liste">
		<summary>Chaos</summary>
		<p>Accumulation désordonnée de gros blocs rocheux effondrés. Paysage de désastre figé, labyrinthique, expressif.</p>
	</details>

	<details class="liste">
		<summary>Talweg</summary>
		<p>Ligne invisible qui court au fond d'une vallée, là où l'eau cherche son chemin. La veine du relief.</p>
	</details>

	<details class="liste">
		<summary>Faille</summary>
		<p>Cassure nette dans la roche, ligne de fracture visible, souvent abrupte d'un côté et à nu. Marque d'un bouleversement.</p>
	</details>

	<details class="liste">
		<summary>Moraine</summary>
		<p>Bourrelet de débris et de pierres transportés par un glacier, déposé en bordure ou en fond de vallée.</p>
	</details>

	<details class="liste">
		<summary>Dos-d'âne</summary>
		<p>Bosse transversale arrondie qui coupe un itinéraire ou rompt la continuité d'une surface plane.</p>
	</details>

	<details class="liste">
		<summary>Verrou</summary>
		<p>Barre rocheuse transversale qui ferme une vallée, forçant les cours d'eau à la franchir en cascade ou à la contourner.</p>
	</details>

	<details class="liste">
		<summary>Coteau</summary>
		<p>Versant doux, légèrement incliné, souvent couvert de végétation rase ou cultivé.</p>
	</details>
</article>

