<article>
	<h2>Vue lointaine</h2>
	<p>Ce qui structure l'horizon – Les grandes masses qui s'imposent d'emblée au regard, même de loin.</p>

	<details class="liste">
		<summary>Montagne</summary>
		<p>Masse de terrain élevée à flancs abrupts, qui domine son environnement et organise tout le paysage alentour.</p>
	</details>

	<details class="liste">
		<summary>Colline</summary>
		<p>Éminence de faible hauteur au sommet arrondi, douce et accessible, sans verticalité menaçante.</p>
	</details>

	<details class="liste">
		<summary>Plateau</summary>
		<p>Vaste étendue plane ou ondulée perchée en hauteur, souvent bordée de versants abrupts. Donne une impression de monde suspendu, d'immensité horizontale.</p>
	</details>

	<details class="liste">
		<summary>Mesa</summary>
		<p>Plateau isolé aux flancs taillés droit, qui surgit seul au milieu d'un paysage ouvert. Évocateur des grands espaces arides et désertiques.</p>
	</details>

	<details class="liste">
		<summary>Plaine</summary>
		<p>Étendue de terrain plate à basse altitude. L'espace qui s'offre — et expose.</p>
	</details>

	<details class="liste">
		<summary>Ondulations</summary>
		<p>Vagues douces et répétées du terrain, sans rupture franche ni verticalité. Suggère le rythme, la continuité, presque la respiration du sol.</p>
	</details>

	<details class="liste">
		<summary>Vallonnement</summary>
		<p>Paysage de petites vallées et de collines douces imbriquées, un terrain qui monte et descend sans jamais vraiment choisir. Évoque un pays habité, une campagne roulante.</p>
	</details>

	<details class="liste">
		<summary>Steppe</summary>
		<p>Grande étendue herbeuse, souvent aride, battue par le vent. Horizon infini, sentiment d'isolement.</p>
	</details>

	<details class="liste">
		<summary>Causse</summary>
		<p>Plateau calcaire sec, à roche souvent apparente, parsemé de chaos et de crevasses. Paysage minéral et austère.</p>
	</details>

	<details class="liste">
		<summary>Massif</summary>
		<p>Ensemble de reliefs élevés formant un bloc homogène, une présence compacte et imposante à l'horizon.</p>
	</details>

	<details class="liste">
		<summary>Contrefort</summary>
		<p>Chaîne secondaire qui s'avance depuis un massif principal, comme un bras tendu vers la plaine.</p>
	</details>

	<details class="liste">
		<summary>Serre</summary>
		<p>Long relief vigoureux et allongé, comme une échine rocheuse qui découpe l'horizon.</p>
	</details>

	<details class="liste">
		<summary>Inselberg</summary>
		<p>Dôme ou massif rocheux isolé surgissant abruptement d'une plaine ou d'un plateau. Présence étrange, presque surnaturelle.</p>
	</details>

	<details class="liste">
		<summary>Piémont</summary>
		<p>Zone de transition entre la plaine et la montagne, là où le terrain commence à se plisser et à s'annoncer. Le relief n'est pas encore là, mais il se prépare.</p>
	</details>

	<details class="liste">
		<summary>Avant-pays</summary>
		<p>Étendue de collines et de reliefs modestes qui précède un massif, comme un avant-garde de la montagne. Le paysage s'agite, se bouscule doucement, avant la verticalité véritable.</p>
	</details>
</article>

<!-- Vue intermédiaire -->
<article>
	<h2>Vue intermédiaire</h2>

	<p>La silhouette qu'on déchiffre en approchant – Les formes qui se précisent à mesure qu'on s'aventure ; ce qui donne sa personnalité au relief.</p>

	<details class="liste">
		<summary>Sommet / Cime</summary>
		<p>Point culminant d'un relief. Ce vers quoi le regard monte, ce que l'on cherche à atteindre ou à éviter.</p>
	</details>

	<details class="liste">
		<summary>Crête / Arête</summary>
		<p>Ligne de faîte séparant deux versants. Tranchante, exposée, souvent battue par le vent.</p>
	</details>

	<details class="liste">
		<summary>Dôme</summary>
		<p>Sommet largement bombé, aux flancs réguliers et continus. Impression de masse sereine et massive.</p>
	</details>

	<details class="liste">
		<summary>Croupe</summary>
		<p>Partie haute arrondie d'une montagne ou d'une colline, comme un dos qui se courbe doucement.</p>
	</details>

	<details class="liste">
		<summary>Aiguille</summary>
		<p>Pic extrêmement effilé, tranchant, dressé vers le ciel. Hostile, inaccessible.</p>
	</details>

	<details class="liste">
		<summary>Piton</summary>
		<p>Pointe de roche isolée, aiguë, formant une proéminence bien distincte au sommet ou sur un flanc.</p>
	</details>

	<details class="liste">
		<summary>Éperon</summary>
		<p>Avancée de terrain allongée et pointue qui se détache d'un ensemble plus massif, comme une langue de roche projetée vers le bas ou vers l'avant.</p>
	</details>

	<details class="liste">
		<summary>Promontoire</summary>
		<p>Lobe de terrain élevé qui s'avance en dominant une étendue — mer, vallée, plaine. Position éminemment cinématique.</p>
	</details>

	<details class="liste">
		<summary>Falaise</summary>
		<p>Paroi quasi verticale, souvent de grande hauteur. Bord absolu, rupture franche entre le haut et le vide.</p>
	</details>

	<details class="liste">
		<summary>Escarpement</summary>
		<p>Versant ou paroi à pente très raide, presque verticale. Moins franc que la falaise, mais tout aussi menaçant.</p>
	</details>

	<details class="liste">
		<summary>Orgues</summary>
		<p>Ensemble de colonnes rocheuses jointives, aux sections régulières et prismatiques, qui strient une paroi ou un relief. Visuellement saisissant.</p>
	</details>

	<details class="liste">
		<summary>Cheminée de fée</summary>
		<p>Colonne de terre ou de roche coiffée d'un bloc plus dur qui l'a protégée de l'érosion. Silhouette irréelle, presque fantastique.</p>
	</details>

	<details class="liste">
		<summary>Col / Brèche</summary>
		<p>Point le plus bas d'une crête, entaille qui permet le passage d'un versant à l'autre. Lieu stratégique par excellence.</p>
	</details>

	<details class="liste">
		<summary>Vallée</summary>
		<p>Dépression allongée creusée entre deux versants, souvent parcourue par un cours d'eau. Le creux qui accueille, abrite ou enserre.</p>
	</details>

	<details class="liste">
		<summary>Combe</summary>
		<p>Vallée en creux allongée, resserrée, encaissée entre des pentes qui se font face. Plus intime et fermée qu'une vallée ordinaire.</p>
	</details>

	<details class="liste">
		<summary>Cirque</summary>
		<p>Vaste hémicycle de montagne en forme de théâtre ou de berceau, aux parois hautes et courbes. Impression d'être cerné, contenu.</p>
	</details>

	<details class="liste">
		<summary>Gorge</summary>
		<p>Vallée étroite et profonde, aux parois abruptes et rapprochées. L'espace comprimé, la lumière rare, le son amplifié.</p>
	</details>

	<details class="liste">
		<summary>Canyon</summary>
		<p>Gorge de très grande ampleur, taillée dans la roche par des siècles d'érosion. Verticalité vertigineuse, couleurs stratifiées.</p>
	</details>

	<details class="liste">
		<summary>Ravin</summary>
		<p>Entaille étroite et abrupte creusée par les eaux. Plus modeste que la gorge, mais tout aussi sauvage.</p>
	</details>

	<details class="liste">
		<summary>Défilé</summary>
		<p>Passage étroit entre des hauteurs, souvent inévitable et défensivement redoutable.</p>
	</details>

	<details class="liste">
		<summary>Cluse</summary>
		<p>Trouée transversale qui perce un relief, comme une porte naturelle ouverte dans la montagne.</p>
	</details>

	<details class="liste">
		<summary>Butte</summary>
		<p>Relief isolé, de taille variable, dominant un terrain plus plat. Peut être tour naturelle ou sentinelle du paysage.</p>
	</details>

	<details class="liste">
		<summary>Tertre</summary>
		<p>Petite éminence de terre, discrète, souvent d'origine mystérieuse ou ancienne.</p>
	</details>

	<details class="liste">
		<summary>Mamelon</summary>
		<p>Petite bosse arrondie et douce, chaude dans sa forme, qui rompt la monotonie d'une pente ou d'une plaine.</p>
	</details>

	<details class="liste">
		<summary>Badlands</summary>
		<p>Étendue chaotique de ravines, de buttes et de crêtes érodées dans des terrains meubles. Paysage dévasté, labyrinthique.</p>
	</details>
</article>

