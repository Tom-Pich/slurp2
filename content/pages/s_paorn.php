<article><!-- Arcania -->
	<h2>Arcania</h2>
	<details>
		<summary class="h3">Présentation</summary>
		<p>L’Arcania est une «&nbsp;police magique&nbsp;» dont le rôle est d’assurer la sécurité de l’empire face aux menaces que constitue l’utilisation de la magie.</p>
		<p>À sa tête se trouve l’Archimage, qui est également conseiller de l’empereur. Celui-ci réside à Olméria.</p>
		<p>
			Toutes les grandes villes de Burgonnie compte un «&nbsp;bureau&nbsp;» de l’Arcania, composé d’un nombre variable de mages (entre trois et une dizaine, selon la taille de la ville). Ces mages ont en charge la surveillance des activités magiques au sein de la ville et, théoriquement, également dans les alentours de la ville.<br>
			Ils assistent les gardes de la ville dans leurs enquêtes ou dans leurs interventions dès qu’une utilisation de la magie est soupçonnée.
		</p>
	</details>
	<details>
		<summary class="h3">Mage de l’Arcania</summary>
		<p>
			Le recrutement des mages se fait généralement dans l’Université des Arcanes d’Olméria. Un mage fraîchement recruté se voit attribuer un nouveau nom (la tradition veut que ce nom finisse en -er), reçoit un tatouage sur le front (l’étoile à sept branches) et doit garder le crâne rasé.<br>
			Il se voit aussi mis sous l’effet d’une <i>Didascalie</i> de niveau V qui l’oblige à rester scrupuleusement honnête en permanence.
		</p>
		<p>
			Les collèges maîtrisés par les mages de l’Arcania peuvent varier, mais ils maîtrisent tous à un bon niveau <i>Connaissances</i> et <i>Communication &amp; Empathie</i>.
		</p>
	</details>
	<details>
		<summary class="h3">Législation, mages &amp; magie</summary>
		<p>Dans l’empire de Burgonnie, pour avoir le droit d’exercer légalement la magie, un mage doit se déclarer auprès de l’Arcania. Après un sondage, celui-ci reçoit alors une <i>Didascalie</i> l’obligeant à rester honnête en toute circonstance. Il reçoit également un anneau (qu’il doit porter) qui constitue le signe visible de son caractère «&nbsp;officiel&nbsp;».</p>
		<p>Un certain nombre de sorts (et de potions) sont illégales. Il s’agit notamment de tous les sorts qui ne peuvent être que «&nbsp;maléfique&nbsp;» – <i>Invocation de démon</i>, <i>Animation des morts</i> en sont deux exemples.</p>
	</details>
	<details>
		<summary class="h3">Mages gris</summary>
		<p>Les mages «&nbsp;gris&nbsp;» sont les mages qui ne sont pas déclarés à l’Arcania. Ils ne sont donc pas sous l’effet de la <i>Didascalie</i> les obligeant à être honnête.</p>
		<p>Ils sont pourchassés par l’Arcania et les peines qu’ils encourent dépendent de leurs actions passées (qui sont révélées grâce à <i>Investigation mentale</i>). Dans tous les cas, la peine est sévère et un mage peut assez facilement être condamné à mort pour ce crime s’il a mené des actions illégales graves.</p>
		<p>Cependant, les mages gris restent assez nombreux. Ils vivent dans de petits villages ou encore plus retirés du monde. Tant qu’ils ne font pas parler d’eux, l’Arcania n’a pas le temps de s’en occuper.</p>
		<p>Il existe également un certain nombre de sorts permettant de fausser les informations obtenues par un mage de l’Arcania grâce à des sorts tels que <i>Lecture d’aura</i> ou <i>Investigation mentale</i>.</p>
	</details>

