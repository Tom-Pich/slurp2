	<details>
		<summary>
			<h3>Déni de service</h3>
		</summary>
		<p>Une attaque par déni de service consiste à submerger un serveur de requêtes non valides qui vont finir par le déborder. Cela va causer un ralentissement important du trafic, voire un plantage du serveur. Un serveur peut être plus ou moins bien configuré pour résister aux attaques de ce type.</p>
		<p>Une attaque de ce type sera d’autant plus efficace que le nombre de machine envoyant des requêtes non valide est élevé. S’il n’y a qu’une seule machine qui attaque, sa puissance doit être nettement supérieure à celle du serveur (ce qui va être généralement difficile) pour pouvoir le déborder.</p>
	</details>

	<details>
		<summary>
			<h3>Techniques d’espionnage</h3>
		</summary>
		<p>Pour faciliter une attaque, il est souvent nécessaire de procéder à une phase d’espionnage préalable.</p>

		<h4>Ingénierie sociale</h4>
		<p>Cette technique consiste à utiliser principalement les «&nbsp;failles humaines&nbsp;» d’un système informatique pour briser ses barrières de sécurité.</p>
		<p>En utilisant ses connaissances, son charisme, son sens de l’imposture ou son culot, l’attaquant cherche à abuser de la confiance, de l’ignorance et de la crédulité de sa cible (l’utilisateur de la machine cible) pour obtenir ce qu’il souhaite (des mots de passe ou des informations permettant une attaque par <i>Force brute</i> beaucoup plus efficace).</p>

		<h4>Sniffing</h4>
		<p>Cette technique nécessite d’être connecté au réseau interne de la cible. Elle consiste à «&nbsp;écouter&nbsp;» les données émises et reçues par la cible. Celles-ci seront généralement cryptées, mais un décryptage a posteriori est envisageable (par force brute). Une partie des données n’est pas cryptée, notamment l’adresse IP de l’émetteur ou du destinataire des données.</p>

		<h4>Hameçonnage</h4>
		<p>Cette technique consiste à détourner la connexion de la cible vers un service <i>fake</i> sans que son utilisateur ne s’en rende compte. Celui-ci communiquera alors des informations avec le fake, en croyant être connecté à un service légitime. Tromper un informaticien avec un fake nécessite beaucoup de talent, mais un utilisateur quelconque pourra être trompé relativement facilement.</p>
		<p>Le détournement de la connexion vers le service fake ne peut se faire en toute transparence uniquement si le pirate est connecté au réseau interne.<br />
			Si ce n’est pas le cas, l’hameçonnage ne pourra être qu’assez grossier et ne trompera que les utilisateurs les plus stupides (ce qui en fait quand-même pas mal).</p>

	</details>

