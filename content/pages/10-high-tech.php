		<h5>Tentatives multiples</h5>
		<p>À chaque tentative après la première, le pirate reçoit un malus de -3 cumulatif à son jet de compétence. Après un échec, il existe une chance (qui dépend du niveau de sécurisation) que la machine-cible détecte une tentative d’intrusion et réagisse en conséquence (selon ce que le programmeur a prévu&nbsp;: déconnexion, effacement des données, pistage de l’attaquant, déclenchement d’un périphérique, etc).</p>

		<h5>Tentative impossible</h5>
		<p>Si le score net de l’attaquant en <i>Piraterie informatique</i> tombe en-dessous de 3, l’attaque est tout simplement impossible. Aucun jet ne pourra réussir (mais le pirate ne le saura pas forcément, il peut faire sa tentative et déclencher une alerte).</p>

		<h4>Procédure d’attaque préparée</h4>
		<p>Si l’attaquant connaît l’existence d’une faille de sécurité dans le système qu’il attaque, il fait son jet comme si le niveau de sécurisation de la cible était de 1.</p>
		<p>Le temps nécessaire et le type d’accès nécessaire dépend de la faille trouvée. Il peut être nécessaire d’avoir un accès physique à l’ordinateur et de disposer de temps pour exploiter une faille.</p>

		<h4>Attaque réussie</h4>
		<p>Si l’attaque réussit, le pirate a accès à l’ordinateur au moins partiellement. L’ampleur du type d’accès dépend de la MR de son jet de <i>Piraterie informatique</i>.</p>

		<table class="left-2">
			<tr>
				<th>MR</th>
				<th>Type d’accès</th>
			</tr>
			<tr>
				<th>0</th>
				<td><b>Accès limité.</b> Il a les autorisations d’un utilisateur lambda et ne peut donc pas accéder aux zones sensibles du système</td>
			</tr>
			<tr>
				<th>1-4</th>
				<td><b>Accès avancé.</b> Il peut accéder à certaines données sensibles et modifier de nombreux paramètres de l’ordinateur ne compromettant pas sa sécurité.</td>
			</tr>
			<tr>
				<th>5+</th>
				<td><b>Accès administrateur.</b> Il peut accéder à l’intégralité de la machine, y compris les fichiers critiques.</td>
			</tr>
		</table>

