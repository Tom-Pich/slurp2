<article>
	<h2>Piratage informatique</h2>

	<details>
		<summary>
			<h3>Objectif d’un piratage</h3>
		</summary>
		<p>Le piratage d’un ordinateur peut avoir trois objectifs différents&nbsp;:</p>
		<ul>
			<li><b>Obtenir un accès illégitime</b> à des données, des programmes ou des périphériques de la cible.<br /></li>
			<li>Faire <b>planter</b> la cible.</li>
			<li><b>Installer du code malveillant</b> (cheval de Troie, virus, ransomware) sur la cible</li>
		</ul>
	</details>

	<details>
		<summary>
			<h3>Paramètres matériels</h3>
		</summary>
		<p>La tentative de piratage dépend des paramètres suivants&nbsp;:</p>
		<ul>
			<li><b>Puissance</b> des ordinateurs impliqués, aussi bien l’ordinateur de l’attaquant que l’ordinateur cible (pour certaines formes d’attaques)</li>
			<li><b>Type d’accès</b> à l’ordinateur cible. Il y a 4 types d’accès possibles.
				<ul>
					<li>Accès <b>physique</b> direct, c’est-à-dire comme un utilisateur classique, via le clavier et l’écran ou l’interface neural. Les possibilités d’interaction avec la machine cible sont extrêmement restreintes.</li>
					<li>Accès <b>par port</b> physique direct, tel qu’une prise USB ou une prise réseau directe. C’est le meilleur type d’accès possible.</li>
					<li>Accès <b>par réseau</b> interne. Le pirate a déjà un accès légitime (ou considéré comme tel) au réseau interne auquel est connecté la machine cible.</li>
					<li>Accès <b>à distance</b>, par Internet. C’est le mode d’accès le plus protégé. La vitesse de connexion va jouer également un rôle important.</li>
				</ul>
			</li>
			<li><b>Temps disponible</b> pour mener la tentative de piratage.</li>
			<li><b>Niveau de sécurisation</b> de l’ordinateur attaqué (voir plus bas).</li>
		</ul>
	</details>

	<details>
		<summary>
			<h3>Niveau de sécurisation d’un ordinateur</h3>
		</summary>
		<p>Le niveau de sécurisation d’un ordinateur dépend des compétences de son administrateur, de la qualité des programmes installés dessus (absence de faille&hellip;) et du que les dispositifs de sécurisation installés (antivirus, mur de feu, etc&hellip;) soient bien à jour.</p>
		<p>On distingue 5 niveaux de sécurisation&nbsp;:</p>
		<table class="left-2">
			<tr>
				<th>Niveau</th>
				<th>Description</th>
			</tr>
			<tr>
				<th>1</th>
				<td><b>Passoire.</b> L’ordinateur est négligé. Ses programmes ne sont pas du tout à jour et rien n’a été fait de particulier pour le sécuriser.</td>
			</tr>
			<tr>
				<th>2</th>
				<td><b>Médiocre.</b> L’ordinateur est négligé. Dispositifs de sécurité de base, pas forcément très à jour. Correspond à l’ordinateur d’un utilisateur peu averti et ne cherchant pas spécialement à le protéger.</td>
			</tr>
			<tr>
				<th>3</th>
				<td><b>Correct.</b> L’ordinateur est correctement protégé. Correspond à l’ordinateur d’un utilisateur averti.</td>
			</tr>
			<tr>
				<th>4</th>
				<td><b>Sécurisé.</b> L’ordinateur bénéficie de mesures particulières de protection. Correspond par défaut à un service publique ne comportant pas de données sensibles.</td>
			</tr>
			<tr>
				<th>5</th>
				<td><b>Haute-sécurité</b> L’ordinateur bénéficie de mesures multiples et de pointe dans le domaine de la protection. Correspond à un service publique contenant des données sensibles ou permettant des transferts d’argent.</td>
			</tr>
		</table>
	</details>

	<details>
		<summary>
			<h3>Force brute</h3>
		</summary>
		<p>C’est le mode d’attaque le plus basique. Il consiste à essayer tous les mots de passe et clés de cryptage possible jusqu’à trouver le bon.</p>
		<p>Si le mot de passe est long et que le niveau de sécurité de l’ordinateur n’est pas faible, le temps nécessaire pour être sûr de trouver le mot de passe est extrêmement long&nbsp;: de 23&thinsp;000 ans (8 caractères) à 1 milliard de milliards de fois l’âge de l’Univers (clé de 128 bits), à raison de 1000 essais par seconde.</p>
		<p>Il faut également tenir compte du fait qu’après un certain nombre de tentatives infructueuses, l’accès peut être bloqué pendant un certain temps et une alerte peut être lancée.</p>
		<p>Ce type d’attaque peut aboutir si le pirate connaît certaines informations sur l’utilisateur (sa date de naissance, le nom de son animal familier&hellip;), que celui-ci a peu sécurisé son ordinateur et que le pirate peut essayer un grand nombre de combinaisons très rapidement et sans être interrompu.</p>

		<h4>Procédure d’attaque</h4>
		<p>Le MJ détermine la longueur du mot de passe (72 possibilités par caractère) ou de la clé de cryptage (en bits).</p>
		<p>Il détermine ensuite, selon le type d’accès, le nombre de tentatives possibles par seconde et si une mesure de sécurité (blocage ou autre) est prise après un certain nombre d’échecs.</p>
		<p>Il détermine enfin le niveau de sécurité du mot de passe (stupide, faible, moyen, fort). Tous les systèmes sécurisés au niveau 4+ auront des mots de passe forts.</p>
		<ul>
			<li>Un mot de passe <b>stupides</b> (typiquement, les mots de passe de WiFi d’hôtels ou de restaurants) nécessite environ 1000 essais, voire deux fois moins si des informations de base sont connues (nom du restaurant, de l’entreprise).</li>
			<li>Un mot de passe <b>faible</b> utilise 0,001&nbsp;% des possibilités offertes, et encore 10 fois moins si des informations personnelles sur l’utilisateur sont connues (date de naissance, nom des membres de la famille, etc&hellip;)</li>
			<li>Un mot de passe <b>moyen</b> utilise 0,1&nbsp;% des possibilités offertes et moitié moins si des informations personnelles sur l’utilisateur sont connues.</li>
			<li>Un mot de passe <b>fort</b> est généré aléatoirement et utilise 100&nbsp;% des possibilités offertes. Il ne sert à rien de connaître une quelconque information sur l’utilisateur.</li>
		</ul>
		<p>À partir de là, le MJ détermine aléatoirement quel pourcentage des possibilités effectives le pirate doit parcourir avant de tomber sur le bon mot de passe.</p>

	</details>

