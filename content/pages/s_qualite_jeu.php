	<h2>Contrat de roleplay</h2>

	<p>Vous aimez visualiser le décor autour de vous, et les PNJ qui sont dedans ? Moi j’aime bien visualiser ce que font les PJ et comment ils se comportent. 😁</p>

	<p>Mais qu’entends-je donc là par roleplay ? Pas une longue tirade qui ralentit le scénar pour parler des confitures de tati Morgane (quoique, de temps en temps et si c’est rigolo…).</p>

	<ul class="flow">
		<li><b>Décrire ce que fait son personnage</b>, de manière à ce que tout le monde le visualise.</li>
		<li><b>Parler en style direct</b> (oubliez les « je lui dis que… »).</li>
		<li><b>Ne pas parler en terme de jeu</b> (« je ne peux pas, j’ai 7 en <i>Escalade</i> ! »)</li>
		<li><b>Agir de manière à avoir une réponse</b>, plutôt que de poser une question directe (ex. « il est profond le trou ? » vs « je m’approche du gouffre et j’essaie de voir le fond »).</li>
		<li><b>Tirer parti de chaque élément de son personnage</b> pour ajouter une touche de réalisme et de profondeur à la scène : son caractère certes, mais aussi ce qu’il porte, son équipement, son état…</li>
		<li><b>Interagir entre PJ</b> et pas seulement entre joueurs.</li>
	</ul>

	<p>Pour ça, bien sûr, il faut connaître son personnage sur le bout des doigts. Alors, je ne dis pas que c’est toujours possible, ni même souhaitable. Mais quand on est dans une scène immersive du scénario, une touche de roleplay rend la partie encore plus sympa pour tout le monde, y compris le MJ.</p>

