	<h2>Les PNJ ont la parole</h2>
	<p>Pour bien différencier les PNJ durant leur prise de parole, inutile de faire systématiquement des accents ou des voix.</p>

	<h4>1. Les paramètres de la voix</h4>

	<ul>
		<li><b>La hauteur :</b> modifier légèrement le ton (plus aigu, plus grave, ou medium).</li>
		<li><b>La force et le volume :</b> parler fort, murmurer (le volume) ou appuyer durement ou mollement sur ses consonnes (la force).</li>
	</ul>

	<h4>2. Le rythme et l’élocution</h4>

	<ul>
		<li><b>La vitesse de parole</b> traduit l’état d’esprit et la personnalité. Débit lent → confiance en soi, calme ou réflexion. Débit rapide → nervosité, anxiété, urgence.</li>
		<li><b>L’élocution</b> peut être fluide, hachée, hésitante, posée, etc.</li>
	</ul>

	<h4>3. « Effets bonus » et tics de langage</h4>

	<p>Pour ajouter du relief à un PNJ, on peut saupoudrer des caractéristiques spécifiques :</p>

	<ul>
		<li><b>Texture vocale :</b> rauque (pour la rudesse), du souffle/de l’expiration (mystère ou fatigue), ou un tremblement (pour la peur – contracter les bras contre son torse aide mécaniquement à faire trembler sa voix).</li>
		<li><b>Voix nasale :</b> idéale pour rendre un PNJ agaçant ou détestable aux yeux des joueurs.</li>
		<li><b>Travers et expressions :</b> tics de langage, manies d’élocution ou une expression signature.</li>
	</ul>

	<h4>4. L’incarnation physique</h4>

	<p>Le corps est le prolongement de la voix. Il aide le MJ à se conditionner et donne un repère visuel immédiat aux joueurs.</p>

	Posture : se tenir très droit (PNJ noble, militaire, fier) ou au contraire voûté et affalé (PNJ fatigué, sournois, timide).
	Langage corporel : jeu des mains, regard, &hellip;

	<h4>La consistance</h4>
	<p>Une fois les paramètres d’un PNJ choisis, il faut impérativement s’y tenir. Sinon, les joueurs perdront le fil et la confusion s’installera autour de la table.</p>

