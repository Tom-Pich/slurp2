		<h5>Messire Aldric d'Ardenvale</h5>

		<p><b>Description Physique&nbsp;:</b> Sir Aldric est un homme robuste, grand et imposant, avec des cheveux blonds coupés court et des yeux bleus perçants. Son visage est marqué par les épreuves qu'il a endurées, et il porte la fière cicatrice d'une bataille passée sur sa joue gauche. Sa posture est droite, reflétant sa détermination et son engagement envers sa foi.</p>

		<p><b>Caractère&nbsp;:</b> Aldric est un homme intègre et dévoué, résolu à protéger les innocents et à défendre la foi. Il est calme et réfléchi, mais sa colère peut s'enflammer face à l'injustice. Il est respectueux envers ses supérieurs et camarades, mais il peut être impitoyable envers ceux qu'il considère comme des ennemis de la lumière.</p>

		<p><b>Équipement&nbsp;:</b> Aldric est revêtu d'une armure lourde polie, ornée d'inscriptions sacrées. Son bouclier est orné du symbole de sa divinité, et il brandit une épée à une main avec une lame ciselée. Il porte également une cape bleue royale, symbolisant sa loyauté envers son ordre.</p>

		<h5>Lexande de Fierbois&nbsp;:</h5>

		<p><b>Description Physique&nbsp;:</b> Lexande est un homme noble d'une beauté frappante, avec des cheveux auburns qui encadrent son visage gracieux, presque féminin. Il a des yeux verts empreints de détermination, et sa présence dégage une aura de confiance. Il est plus petite que Sir Aldric, mais sa prestance n'en est pas moins impressionnante.</p>

		<p><b>Caractère&nbsp;:</b> Lexande est un chevalier déterminé et courageux, prête à tout pour protéger les faibles. Il est loyal envers ses convictions et ses compagnons d'armes. Il est également diplomate, capable de parler avec sagesse et fermeté. Sa foi est inébranlable, et il cherche constamment à s'améliorer pour mieux servir sa cause.</p>

		<p><b>Équipement&nbsp;:</b> Lexande porte une armure élégante mais robuste, incrustée de motifs symboliques. Il manie une lance avec habileté, symbolisant sa ferveur dans la bataille. Son bouclier est orné du blason de sa famille, et il arbore une cape rouge sang, signe de sa volonté de verser son propre sang pour protéger les innocents.</p>

