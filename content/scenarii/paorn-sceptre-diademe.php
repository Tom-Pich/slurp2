			Il a également recueilli un orphelin nommé <i>Vyncis</i> alors qu’il avait neuf ans. Son père était mort en mer et sa mère a succombé à la maladie.<br>
			Hélas, cet orphelin n’avait aucune disposition pour la magie. Quand Trébal est mort, il y a une cinquantaine d’années, Vyncis, qui avait 15 ans à ce moment, a continué à vivre dans la cabane de Trebal et a essayé, tant bien que mal, de comprendre ses recherches. Aujourd’hui, l’orphelin est devenu un vieil homme. Il sait beaucoup de choses, notamment sur les Araéthias. Ses connaissances sont purement théoriques, tirées des travaux de Trebal, car il n’a pas la condition physique pour partir à l’aventure (il est boiteux et a une santé fragile).
		</p>

		<h4>Bâtiments notables</h4>
		<ul>
			<li>La petite église au clocher effilé domine le village, témoignant de la foi profonde des habitants.</li>
			<li>Une forge</li>
			<li>Une taverne</li>
		</ul>

		<h4>Taverne "La Mélodie des Vagues"</h4>

		<p>Située au cœur de Difeld, la taverne "La Mélodie des Vagues" est un lieu chaleureux où les voyageurs et les villageois se retrouvent pour partager des récits, des rires et des verres bien mérités. La taverne doit son nom à la proximité de la mer, dont le doux murmure accompagne les conversations animées à l'intérieur.</p>

		<h5>Propriétaire : Rosalind Havenvale</h5>

		<p>Rosalind Havenvale, une femme d'âge moyen au sourire accueillant, est la propriétaire bien-aimée de "La Mélodie des Vagues". Ses cheveux bruns encadrent son visage bienveillant, et ses yeux pétillent d'une lueur amicale. Elle est connue pour sa jovialité et son talent pour créer une atmosphère conviviale.</p>

		<p>Rosalind, originaire de Difeld, a hérité de la taverne de ses parents et a depuis transformé l'endroit en un refuge chaleureux pour les habitants et les visiteurs. Elle est une maîtresse de taverne attentionnée, veillant à ce que tous se sentent les bienvenus dans son établissement. Elle est également connue pour sa cuisine simple mais délicieuse, mettant en avant les produits locaux, notamment les poissons fraîchement pêchés.</p>

		<p>La Mélodie des Vagues est ornée de décorations marines et de poutres en bois usé par le temps. Les tables en bois massif sont souvent remplies de pêcheurs racontant leurs histoires de mer, de villageois partageant les potins du jour et de voyageurs cherchant un abri dans cette enclave accueillante. Le crépitement du feu dans la cheminée ajoute une ambiance chaleureuse aux soirées animées de la taverne.</p>

		<p>Rosalind dispose de quelques chambres aux confort sommaire pour les voyageurs de passage.</p>

		<h4>PNJ notables</h4>

		<h5>L'ancien - Donato</h5>
		<p>
			Donato est un vieil homme borgne au visage creusé par les rides et le temps. Ses cheveux argentés encadrent un regard perçant qui semble toujours être en train de réfléchir. Bien que chancelant par moments, il conserve une dignité et une sagesse qui inspirent le respect.<br>
			Donato est le chef du village, occupant la position d'ancien. Sa longue expérience et sa connaissance de la mer font de lui une autorité naturelle. Les villageois viennent souvent le consulter pour obtenir des conseils.
		</p>

		<h5>Le Forgeron - Isabella</h5>
		<p>Isabella est une femme robuste avec des mains puissantes et des cheveux noirs noués en un chignon pratique. Son tablier en cuir est constellé de taches de suie, témoignant de son travail acharné à la forge.</p>

		<h5>Le Curé - Père Mateo</h5>
		<p>
			Le Père Mateo est un homme au visage bienveillant et à la barbe grisonnante. Il porte souvent une soutane sombre et un chapeau assorti, se tenant droit avec une démarche assurée.<br>
			En tant que curé, Père Mateo est le guide spirituel de Difeld. Il veille sur la moralité du village, officie les cérémonies religieuses et offre du réconfort à ceux qui en ont besoin. Les villageois le considèrent comme un pilier de stabilité dans des temps parfois tumultueux.
		</p>

		<h5>Vyncis</h5>
		<p>Vyncis est un homme de constitution fragile, fatigué par les années et usé par le poids des échecs. Son visage émacié révèle les traces profondes d'une vie de lutte contre la maladie et la fatigue. Les cheveux grisonnants encadrent un visage marqué par des rides prononcées, témoignage de la frustration de ne jamais avoir été capable de maîtriser la magie malgré son désir ardent.</p>

		<p>Sa démarche, légèrement boiteuse, semble accentuer sa vulnérabilité physique. Les yeux jadis vifs sont maintenant ternes, portant le fardeau de nombreuses nuits passées à essayer de comprendre les mystères laissés par son mentor, Trébal. Bien que la fatigue soit palpable, Vyncis demeure un gardien déterminé des connaissances du village, malgré son incapacité à manier les arts magiques qui ont défini la vie de Trébal.</p>

