	<details><!-- Mission pour Jaruzok -->
		<summary>
			<h3>En mission pour Jaruzok</h3>
		</summary>
		<p class="important"><b>Objectifs de la scène&nbsp;:</b> les PJ doivent se débrouiller, dans la plus grande discrétion, pour aller voler des produits chimiques dans une usine.</p>
		<p>Au matin de J3, un des PJ reçoit un SMS envoyé par un numéro privé&nbsp;: il s’agit d’un lien internet et d’un code. Le lien mène à un hébergement privé d’où ils peuvent visualiser des extraits de vidéos prises alors qu’ils étaient dans l’appartement de Julie. Si besoin, ces vidéos peuvent être truquées pour les rendre encore plus compromettantes, mais il est préférable qu’elles soient authentiques si c’est suffisant.</p>
		<p>Alors que les PJ commencent à paniquer, un deuxième message arrive&nbsp;: il s’agit d’une liste de «&nbsp;courses&nbsp;»&nbsp;: 20&nbsp;L d’acide sulfurique concentré, 80 kg de perchlorate de potassium et 200 kg d’engrais. Il y a également une adresse&nbsp;: Interchimie, 13 Rue Louis Blériot, 77290 Compans. Les PJ ont deux jours pour obtenir ces produits.</p>

		<p>Les PJ ont plusieurs options pour obtenir les produits chimiques demandés :</p>

		<h4>Approche directe à Interchimie :</h4>
		<p>Les PJ peuvent se rendre à l'adresse indiquée et tenter d'obtenir les produits chimiques de manière légitime. Cela pourrait nécessiter la création d'une fausse entreprise ou d'une fausse identité professionnelle.</p>

		<h4>Infiltration discrète :</h4>
		<p>Les PJ pourraient opter pour une approche plus furtive, infiltrer l'usine lorsqu'elle est fermée, et voler les produits chimiques sans éveiller de soupçons. Si les PJ ont des compétences en piratage, ils pourraient essayer de désactiver temporairement les systèmes de sécurité de l'usine pour faciliter l'accès.</p>

		<h4>Connexions locales :</h4>
		<p>Les PJ pourraient essayer de trouver des contacts locaux qui pourraient leur fournir les produits nécessaires sans attirer l'attention.</p>

