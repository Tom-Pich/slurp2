	<h2>Scénario</h2>

	<details><!-- Contexte & cause -->
		<summary>
			<h3>Contexte &amp; causes</h3>
		</summary>
		<p>Jaruzok, alias Gauthier Allard, et ses deux associés, Guillaume Roux et Julie Magnier, sont des démons basés à Paris. Ils poussent des humains à la faute (ou attendent qu’ils en commettent spontanément), les faisant chanter par la suite pour leur soutirer services ou argent. Confusion, cupidité et humour noir : un trio démoniaque pour remplir les caisses des forces du Mal et étendre leur influence.</p>
		<p>Le trio a piégé le préfet de Police, Laurent Nuñez, pour une histoire de sex-tape. Prétextant être un membre de la DGSI qui « en a marre de la paperasse à n’en plus finir », il a négocié avec le préfet un accès aux fichiers centraux de la Police Nationale en échange de son silence.</p>
		<p>Il y a quelques jours, Jaruzok reçoit l’ordre de forcer Al-Jan à organiser un attentat à Paris. Le trio monte un plan pour piéger Al-Jan et le faire chanter par la suite. Magnier se fait passer pour une escort-girl. Une fois dans la chambre d’Al-Jan, elle le drogue et le sodomise avec un saucisson devant une caméra. Le lendemain, le trio commence son chantage. Avec l’argent destiné aux frères musulmans, Al-Jan doit organiser un attentat sur Paris.</p>
		<p>Al-Jan est fou de rage. Il doit se plier aux exigences du maître-chanteur, tout en cherchant à retrouver Julie Magnier. Magnier a laissé le téléphone de Jaruzok (son familier) pour pouvoir communiquer avec lui. Il est discrètement suivi tantôt par Julie Magnier, tantôt par Guillaume Roux.</p>
		<p>Laurent Nuñez, de son côté, n’en est pas resté là non plus. Grâce aux services de police, il parvient à remonter une piste qui le mène à l’appartement de Julie. Après une courte enquête officieuse, il s’avère que cette Julie Magnier n’existe pas. Tout ceci finit par arriver dans le colimateur des forces du Bien.</p>
		<p>Al-Jan rencontre un prédicateur dans une mosquée de banlieue pour recruter des hommes de mains. Jaruzok, de son côté, lui indique qu’il se charge de lui fournir les précurseurs nécessaires à la fabrication d’explosif (une des missions des PJ).</p>
		<p>C’est à ce stade que le scénario commence.</p>
	</details>

	<details><!-- Synopsis -->
		<summary>
			<h3>Synopsis</h3>
		</summary>
		<p>Les PJ viennent de s’incarner. Ils sont envoyés sur une première mission basique : vérifier si Julie Magnier est une humaine ou un démon et l’éliminer dans ce dernier cas.</p>
		<p>Lors de cette mission, ils se feront repérer par Jaruzok comme étant des anges (vidéosurveillance de l’appartement).</p>
		<p>Jaruzok prend contact avec les PJ et menace de publier des vidéos compromettantes, ce qui mettra à mal les PJ par rapport au Principe de discrétion. En échange de son silence, les PJ vont devoir se procurer des précurseurs d’explosifs dans une usine chimique (ou grâce à la complicité des forces du bien, s’ils n’ont pas peur de faire mauvaise impression pour leur première mission).</p>
		<p>D’un autre côté, les PJ peuvent également essayer d’enquêter sur les victimes de chantage dans l’espoir de remonter jusqu’au maître-chanteur.</p>
	</details>

