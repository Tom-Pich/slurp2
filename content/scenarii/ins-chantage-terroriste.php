	<details> <!-- Réaction du trio -->
		<summary>
			<h3>Réaction du trio à l’intrusion des PJ</h3>
		</summary>

		<p class="important">
			<b>Objectifs de la scène&nbsp;:</b> Les PJ doivent être victimes du plan diabolique de Jaruzok&nbsp;: se faire filmer en train d’utiliser leurs pouvoirs, pour qu’il puisse excercer son chantage.
		</p>

		<p><b>Guillaume Roux</b> est envoyé pour se mettre en planque devant l’immeuble de Julie. Il dispose d’un gros scooter sur lequel il a installé un top-case <i>Ubereats</i>.</p>

		<p><b>Julie</b>, quant à elle, ne retourne pas vivre dans son appartement. C’est trop risqué. En utilisant son pouvoir <i>Changement d’apparence</i>, elle va enquêter dans son propre immeuble pour obtenir des informations sur les PJ (nombre, description physique&hellip;)</p>

		<p>Une fois ces informations collectées, il faut attirer les PJ dans l’appartement de Julie en leur faisant croire qu’elle est retournée chez elle. Lorsqu’ils seront dans son appartement, ils doivent être poussés à utiliser un pouvoir peu discret. Pour autant, Julie refuse de prendre le risque d’affronter les PJ.</p>

		<h4>Le plan Camille Laporte</h4>
		<p>
			Julie aborde Camille Laporte, une jeune femme seule qui habite le voisinage. Elles vont boire un café dans le quartier. Prétendant être nouvelle dans le quartier et cherchant à se faire des amis, Julie, utilisant son pouvoir de <i>Charme</i>, promet à Camille une expérience unique et passionnante, sans entrer dans les détails. Elle joue sur la curiosité et l'envie d'aventure de Camille. Julie mentionne que cette soirée spéciale est liée à des événements mystérieux dans le quartier, créant une intrigue autour de son appartement sans donner trop d'informations. Elle suggère à Camille que ça pourrait être une opportunité de vivre quelque chose de différent et d'excitant.
		</p>

		<p>Camille, influencée par le charme de Julie et attirée par la promesse d'une expérience unique, accepte d'aller passer une soirée dans l'appartement.</p>
		
		<p>L’objectif du trio est de pousser les PJ à utiliser des pouvoirs visibles et de les filmer en train de le faire.</p>
		
		<p>Une fois les PJ à l’intérieur de l’appartement, Jaruzok et Julie les observent par le sytème de vidéosurveillance. Tout est enregistré. Au moment opportun, ils passent un coup de fil sur la ligne fixe de l’appartement et demande aux PJ un numéro de portable où les joindre plus tard. S’ils ne le font pas, leur signalement et leurs actes seront rendus public.</p>
		
		<p>Si les PJ n’utilisent pas leurs pouvoirs spontanément, Julie a placé une enceinte connectée dans sa chambre. Elle pourra diffuser, au bon moment, un bruit de talkie-walkie et un ordre clair&nbsp;: «&nbsp;ils sont là, neutralisez-les&nbsp;!&nbsp;».</p>

		<p>Pendant ce temps-là, Guillaume Roux est aussi en planque dans la rue, avec son scooter.</p>


		<h4>Actions possibles des PJ</h4>
		<p>Si les PJ sont en planque devant l’immeuble, le soir de J1 ils voient Camille Laporte entrer dans l’immeuble, vers 21 h. Cela pourrait éveiller leurs soupçons.</p>
		<p>Camille a les clés de l’appartement de Julie, qui est censée la rejoindre un peu plus tard. Elle est toute émoustillée à l’idée de la «&nbsp;soirée mystérieuse&nbsp;» qu’elle va passer, comme le lui a promis Julie.</p>
		
		<h4>Interroger Camille</h4>
		<p>Selon la manière dont les PJ entrent dans l’appartement de Julie, Camille pourra trouver cela normal (soirée mystérieuse) ou tout à fait effrayant (effraction discrète).</p>

		<p><b>Pourquoi elle est là :</b> Camille explique aux PJ qu'elle a rencontré Julie récemment, qu'elles ont sympathisé, et que Julie l'a invitée à passer une soirée entre amies pour discuter de choses mystérieuses dans le quartier. Julie n’est pas là. Elle lui a confié les clés de son appartement et lui a dit qu’elle la rejoignait plus tard dans la soirée.</p>
		<p><b>Ce qu'elle sait :</b> Elle ne sait pas exactement ce qui l'attend, mais elle a été intriguée par l'idée d'une expérience unique.</p>
		<p><b>Sa relation avec Julie :</b> Camille ne connaît pas Julie depuis longtemps et c'est leur première soirée ensemble, mais que Julie lui a semblé sympathique et intrigante.</p>

		<h4>Lorsque les PJ sortent</h4>
		<p>Guillaume Roux est seul, les PJ sont trois. Il sait que ça serait beaucoup trop risqué pour lui d’intervenir. Par contre, il surveille la sortie des PJ du bâtiment, et pourra éventuellement les suivre (à pied, ou en scooter).</p>

