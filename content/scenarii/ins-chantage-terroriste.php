		<p>L’appartement de Julie Magnier est situé au 5<sup>e</sup> et dernier étage du <a href="https://www.google.fr/maps/place/20+Rue+du+Moulinet,+75013+Paris/@48.8271313,2.3544447,18z/data=!3m1!4b1!4m6!3m5!1s0x47e671a596a871b9:0xfeb4d35528b87c83!8m2!3d48.8271301!4d2.3553385!16s%2Fg%2F11sc1wclfn?entry=ttu">20, rue du Moulinet</a> dans le XIIIe. Petit F2 (salon cuisine + chambre).</p>

		<p>L’appartement est vidéosurveillé. De jour, Julie n’est pas là. Elle rentre le soir après qu’Al-Jan se couche, vers 23 h. Si les PJ pénètrent dans son appartement en son absence, elle sera immédiatement avertie par le système de vidéosurveillance et ne reviendra pas chez elle. Elle a toujours sur elle son tout petit ordinateur (qui contient de nombreuses infos sur les victimes du chantage du trio). Pour la cueillir chez elle, il faut venir de nuit.</p>

		<p>Lors de la fouille de l’appartement, si les PJ s’en prennent à Julie&nbsp;: coup de fil sur le téléphone fixe. C’est Jaruzok qui leur signale que tout a été filmé et qui exige un numéro de téléphone pour les joindre à tous moments.</p>

		<p>Parmi la liste des personnes victimes de chantage : le préfet de police, Al-Jan, mais aussi des journalistes célèbres, des hommes politiques, des magistrats… Ces informations sont accessibles sur l’ordinateur portable de Julie, mais il faut son empreinte pour le déverrouiller. Il faudra l’équipe technique du QG pour obtenir ces informations.</p>

		<p>Si les PJ visitent l’appartement de Julie en son absence, le trio deviendra extrêmement méfiant. Jaruzok tendra un piège aux PJ (voir le paragraphe suivant).</p>

