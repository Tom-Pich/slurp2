	<details><!-- Introduction -->
		<summary>
			<h3>Introduction</h3>
		</summary>
		<p>Les PJ sont fraîchement incarnés. L’équipe d’accueil leur remet des vêtements, des faux papiers, un peu de cash et un téléphone, puis les accompagne vers leur logement. Les deux nouveaux venus logent dans un petit appartement meublé au 8, rue Aubricot et doivent attendre un coup de fil pour une première mission. Marie Uslaub est logée au 60 rue des Lombard.</p>
		<p>Le lendemain, convocation au QG, rencontre avec Loïc Lemoine.</p>
		<p><b>Première mission pour se faire la main&nbsp;:</b> une femme supectée d’être un démon a été repérée. Les PJ doivent vérifier sa nature et l’éliminer dans la plus grande discrétion s’il s’agit d’un démon.</p>
	</details>

