<p>L’Artaille est la province la plus méridionnale de la Burgonie.</p>

<h3>Duché d’Elmour</h3>

<p>
	Le <i>duché d’Elmour</i>, situé dans la province de l'Artaille, comprend les villes d'Imégie, d’Almisie et de Stomilie. Il s’étend sur environ 65.000 km². La <i>Côte sauvage</i>, au sud (14.000 km² supplémentaires), en fait également théoriquement partie, bien qu’elle soit hors de contrôle des forces princières. Cette côte s'étend sur environ 220 km entre Imégie et Maylie.<br>
	Entre ces trois villes, la densité de population est élevée.
</p>

<p>Échelle de la carte&nbsp;: la distance entre Almisie et Imégie est de 110 km.</p>

<p>Les terres sont fertiles et le climat est doux et sec sans être aride. Paysages vallonnés avec des forêts méditerrannéennes, du maquis de la garrigue et des zones cultivées.</p>

<p>
	La <b>péninsule de Lorendrar</b> est située entre Imégie et Stomilie. Principalement occupée par les monts Thundrak, elle ne compte que très peu d’habitants humains – principalement des parias et des brigands. Le nord de la péninsule, dans la baie de port Goshal, compte néanmoins des villages de pêcheurs.<br>
	C’est une zone assez hostile, balayée par les vents. L’herbe n’est pas bonne pour les bêtes et pas grand chose ne peut y être cultivé.
</p>

<h3>Stomilie (ville)</h3>
<figure class="wiki-right-element clear-right mt-1">
	<img src="/assets/img_paorn/stomilie_01.jpg" alt="">
	<figcaption>Stomilie et sa cathédrale fortifiée</figcaption>
</figure>
<p>
	Stomilie est une ville fortifiée d’environ 10.000 habitants, de forme globalement circulaire.<br>
	Située à 10 km de la mer, elle est construite sur une colline.<br>
	Elle est considérée comme une ville sainte de l’Atrimisme, car c’est non loin de cette ville que le prohète Eldred est né, et son tombeau se trouve dans cette ville.<br>
	Construite sur un terrain en pente, elle comporte de nombreux escaliers.<br>
	Une petite rivière aux eaux réputées particulièrement pures jaillit de la colline<br>
	Le seigneur des lieux est l’archiprêtre de Stomilie&nbsp;: Monseigneur Giorgio de Scamora, duc et archiprêtre.
</p>

