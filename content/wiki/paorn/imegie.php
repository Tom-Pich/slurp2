	<h5>La Vieille Ville</h5>
	<p>Cette partie de la citée est ceinte par une muraille intérieure. Il ne s’agit pas d’un quartier à proprement parler, mais d’un ensemble de quatre quartiers&nbsp;: la <i>Citadelle</i>, le <i>quartier des Consuls</i> juste après (en rouge), <i>Bellevue</i> (en vert), et enfin <i>Saint-Almer</i> à l'extrême sud-ouest (en jaune). La vieille ville a une superficie de 60 ha environ, et compte 8000 habitants. C’est la partie la plus riche de la ville et la sécurité y est particulièrement renforcée.</p>

	<p><b>Quartiers des Consuls&nbsp;:</b> il abrite des bâtiments officiels (tribunal, les douanes, palais du peuple &ndash; pour les services administratifs de la ville&nbsp;: impôts, voirie, offices divers)</p>

	<p><b>Quartier de la Citadelle&nbsp;:</b> il s'agit essentiellement d'un quartier militaire (avec la caserne principale de la ville). On y trouve également les Écuries du Prince.</p>

