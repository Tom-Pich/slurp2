<h3>Points de personnage</h3>

<p>Un personnage se construit à partir de points de personnage (généralement entre 80 et 120 pts, voyez ça avec votre MJ).</p>
<p>Ces points de personnage permettent de définir les <b>Caractéristiques</b>, <b>Avantages, Désavantages &amp; Travers</b>, <b>Compétences</b> et enfin les <b>Sorts &amp; pouvoirs</b> traités dans les parties <i>Magie</i>, <i>Psioniques</i> et/ou <i>Univers</i>.</p>

<h3>Concevoir son personnage</h3>

<p>Avant de se lancer dans l’aspect technique de sa création, il est préférable de commencer par l’ébaucher.</p>

<details>
	<summary class="fw-700">Nom</summary>
	<p>Éviter les clichés et respecter l’ambiance de l’univers de jeu.</p>
</details>

<details>
	<summary class="fw-700">Métier &amp; position social</summary>
	<ul class="ta-left">
		<li>Gagne-pain / métier</li>
		<li>Race, ethnie, nationalité, origine</li>
		<li>Statut social</li>
		<li>Niveau d’étude</li>
		<li>Richesse</li>
	</ul>
</details>

<details>
	<summary class="fw-700">Description</summary>
	<ul class="ta-left">
		<li>Apparence physique</li>
		<li>Manière de se vêtir</li>
		<li>Attitude physique</li>
	</ul>
</details>

<details>
	<summary class="fw-700">Caractère &amp; comportement</summary>
	<ul class="ta-left">
		<li>Tempérament, personnalité (voir des exemples de <i><a href="/traits-de-caracteres">Traits de caractères</a></i>)</li>
		<li>Moralité</li>
		<li>Goûts et dégoûts</li>
		<li>Comportement, attitude</li>
		<li>Problèmes psychologiques</li>
		<li>Tic et citation</li>
	</ul>
</details>

<details>
	<summary class="fw-700">Motivations &amp; objectifs</summary>
	<ul class="ta-left">
		<li>Raison pour laquelle il fait son métier ou part à l’aventure</li>
		<li>Objectifs à long terme</li>
		<li>Croyances (religion, idéaux, etc)</li>
	</ul>
</details>

<details>
	<summary class="fw-700">Background</summary>
		<ul class="ta-left">
			<li>Famille (origine, composition, niveau social)</li>
			<li>Enfance (conditions, éducation)</li>
			<li>Adolescence (conditions, études ou apprentissage)</li>
			<li>Vie amoureuse, vie de famille</li>
			<li>Vie professionnelle</li>
			<li>Déboires, aventures, hauts et bas</li>
			<li>Relations (famille, amis, ennemis...)</li>
			<li>Domicile (localisation, type, vit-il seul...)</li>
		</ul>
</details>

