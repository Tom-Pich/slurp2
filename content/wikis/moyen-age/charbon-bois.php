<section>
	<h2>Usage domestique et urbain</h2>

	<p>Si les industries lourdes absorbent des volumes colossaux, le charbon de bois est également un <b>produit de consommation de masse en milieu urbain</b>. Son utilisation répond à des impératifs d’aménagement et de salubrité propres aux villes médiévales densément peuplées.</p>

	<h3>Le combustible d’intérieur</h3>

	<p>La principale qualité domestique du charbon de bois est sa <b>combustion sans fumée</b> ni flamme rousse. Dans les habitations citadines, souvent dépourvues de cheminées à conduit vertical, le chauffage s’effectue au moyen de braseros déplaçables de pièce en pièce sans risque d’asphyxie par les fumées.</p>

	<p>En <b>cuisine</b>, il est le carburant privilégié des potagers et réchauds en terre cuite. Il permet des cuissons douces et régulières, indispensables aux mijotages, sans altérer le goût des aliments par les essences de bois et sans encrasser les habitations de suie.</p>

	<h3>Une fracture sociologique</h3>

	<p>L’accès à l’énergie révèle une nette distinction sociale au Moyen Âge :</p>

	<p>Le <b>monde rural</b> consomme presque exclusivement du bois brut, ramassé localement (droits d’affouage et de mort-bois), accepté malgré les désagréments des fumées et l’encombrement des foyers ouverts.</p>

	<p>Le <b>monde urbain et aristocratique</b> dépend du charbon de bois. Bien que plus coûteux en raison des taxes seigneuriales (tonlieux) et du coût du transport (par convois de mulets ou protégé au sec sur les grands trains de bois), il s’impose comme un produit de première nécessité pour la bourgeoisie et les corps de métiers citadins (orfèvres, apothicaires, tailleurs).</p>
</section>