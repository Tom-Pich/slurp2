<p>Le charbon de bois constitue le principal vecteur d’énergie concentrée de la société médiévale. Pivot de l’économie proto-industrielle et du confort urbain, il se distingue du bois brut par sa densité calorifique et sa pureté chimique, acquises au terme d’un processus de transformation long et rigoureux.</p>

