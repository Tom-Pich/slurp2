<section>
	<h2>Rendements et propriétés</h2>

	<p>La transformation du bois vert en charbon de bois s’accompagne d’une perte massive de matière : il faut en moyenne <b>5 tonnes de bois vert</b> pour produire <b>1 tonne de charbon de bois</b>.</p>

	<p>En bilan global, la fabrication du charbon de bois engendre une perte de près de 50 % de l’énergie thermique initiale. <b>1 kg de charbon</b> de bois contient la même quantité d ’énergie thermique que <b>2 kg de bois sec</b>, mais il la libère de manière beaucoup plus concentrée.</p>

	<p>La combustion du charbon permet d’atteindre des <b>températures plus élevées</b> (1&thinsp;200 °C à 1&thinsp;300 °C) que celle du bois sec (800 °C à 900 °C).</p>
</section>

