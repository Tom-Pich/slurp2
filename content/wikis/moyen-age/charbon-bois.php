<section>
	<h2>La carbonisation</h2>

	<p>Le charbon de bois est fabriqué par pyrolyse du bois vert en atmosphère contrôlée, privée d’oxygène. Cette opération est menée au cœur des massifs forestiers par les charbonniers.</p>

	<figure>
		<img src="/assets/img_moyen_age/meule-charbon.jpg" alt="photo d’une meule de charbonnier">
		<figcaption>Structure interne d’une meule de charbonnier</figcaption>
	</figure>

	<p>Le procédé se déroule en plusieurs phases :</p>

	<ul class="flow">
		<li><b>Le montage :</b> autour d’un poteau central servant de cheminée, des bûches de bois dur (principalement du chêne, du hêtre ou du charme) sont empilées verticalement et de manière concentrique pour former un dôme appelé meule (ou fourneau), pouvant atteindre quatre à six mètres de diamètre.</li>
		<li><b>La couverture :</b> afin d’isoler le bois de l’air extérieur, la meule est intégralement recouverte d’une couche végétale (mousse, feuilles), puis scellée par une épaisse chape de terre humide ou de gazon.</li>
		<li><b>La cuisson :</b> le foyer est allumé par le sommet. La combustion interne dure entre cinq et dix jours et exige une surveillance constante. Le charbonnier régule l’apport d’air en perçant ou en obstruant de petits trous d’évent à la base de la structure.</li>
	</ul>

	<p>L’art du charbonnier repose sur la lecture de la fumée s’échappant de la meule. Une fumée blanche indique l’évaporation de l’eau du bois ; une fumée jaune et épaisse signale une combustion trop rapide (risque de calcination complète) ; une fumée bleue et transparente marque la fin de la carbonisation.</p>

	<p>Le bois utilisé était du bois <i>vert</i>. L’utilisation du bois sec ne permettrait pas le contrôle de la combustion : la meule flamberait en quelques heures.</p>
</section>

