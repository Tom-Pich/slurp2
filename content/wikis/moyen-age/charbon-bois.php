<section>
	<h2>Usage en métallurgie</h2>

	<p>L’utilisation du charbon de bois est une condition <i>sine qua non</i> au développement de la sidérurgie médiévale. Le fonctionnement des bas fourneaux (puis des premiers hauts fourneaux à la fin du Moyen Âge) dépend entièrement de ce combustible.</p>

	<p>Dans le fourneau, le charbon de bois remplit un double rôle :</p>

	<ul>
		<li><b>Thermique :</b> il génère la chaleur extrême nécessaire à la fusion des scories et au ramollissement du fer.</li>
		<li><b>Chimique :</b> en brûlant, il produit du monoxyde de carbone qui réduit les oxydes de fer contenus dans le minerai, permettant l’extraction du métal sous forme de « loupe ».</li>
	</ul>

	<p>Contrairement au charbon de terre (la houille, connue au Moyen Âge), le charbon de bois est <b>chimiquement pur</b> (exempt de soufre et de phosphore). Il évite ainsi de contaminer le fer, ce qui le rendrait cassant et impossible à forger.</p>
</section>

