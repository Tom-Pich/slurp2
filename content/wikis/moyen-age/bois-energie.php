<p>Le bois est <i>la</i> source principale d’énergie au Moyen Âge, en plus d’être une matière première fondamentale. Il sert à cuisiner, se chauffer, produire des métaux à partir de minerai, fabriquer du verre, extraire le sel de l’eau de mer et à construire navires, châteaux et cathédrales.</p>

