<!-- Usages proto-industriels -->
<section>
	<h2>Usages proto-industriels</h2>

	<h3>La métallurgie</h3>
	<p>Un seul bas fourneau ou une forge d’importance moyenne consomme entre <b>1&thinsp;200 et 2&thinsp;000 m³</b> de bois par an. Exploiter une mine de fer nécessite de posséder et de raser cycliquement des centaines d’hectares de forêts aux alentours.</p>
	<p>Cette consommation de bois (qui passe par la production de <a href="/wiki/moyen-age/charbon-bois">charbon de bois</a>) permet de produire environ <b>40 à 70 tonnes de fer</b> doux par an (ou 70 % de cette masse en acier, qui nécessite l’étape de cémentation).</p>

	<h3>Les autres industries</h3>
	<p>Les verreries et les sauneries (les usines d’évaporation du sel) sont tout aussi destructrices. Au XV<sup>e</sup> siècle, on estime qu’il fallait <b>1 m³ de bois</b> pour produire seulement <b>1 kg de verre</b> !</p>
</section>

<!-- Grandes constructions -->
<section>
	<h2>Grandes constructions</h2>

	<p>Ici, on parle de bois d’œuvre (de gros troncs de chênes centenaires), qui répond à une autre logique de gestion forestière (la haute futaie).</p>

	<p><b>Les charpentes :</b> pour construire la charpente d’une cathédrale ou d’un grand château, il faut abattre environ <b>1&thinsp;000 à 1&thinsp;200 chênes</b>.</p>

	<p><b>Les navires :</b> un seul grand navire de guerre ou de commerce réclame plusieurs <b>centaines de grands arbres</b>. La coque exige des bois tors (arbres naturellement courbés, indispensables pour la solidité des membrures), tandis que la mâture réclame des résineux parfaitement droits.</p>
</section>

<!-- Le transport du bois -->
<section>
	<h2>Le transport du bois</h2>


	<p>Transporter du bois par voie de terre au Moyen Âge est un gouffre financier. Une charrette tirée par quatre bœufs s’embourbe, avance lentement et ne peut transporter que deux ou trois gros troncs. Dès que la forêt est à plus de 15 ou 20 kilomètres du lieu de consommation, l’eau devient le seul moyen de transport rentable.</p>


	<h3>Le flottage à bûches perdues</h3>

	<p>Cette technique est réservée au bois de chauffage. Les bûches sont coupées courtes (environ 1 mètre) dans les forêts de montagne et jetées directement dans les torrents au printemps, au moment de la fonte des neiges (la « crue de bois »). À l’arrivée, on installe de grands barrages de cordes et de pieux (les râteaux) en travers du fleuve pour arrêter et repêcher les milliers de bûches qui flottent.</p>

	<h3>Le flottage en « trains de bois »</h3>

	<p>C’est la méthode reine pour le bois d’œuvre (les grands troncs pour la construction). Les charpentiers assemblent les troncs directement dans l’eau pour former d’immenses radeaux articulés (les trains), qui peuvent mesurer jusqu’à 70 mètres de long. Des compagnons « flotteurs » montent à bord et dirigent ces monstres à la rame au fil du courant sur les grands fleuves. C’est un métier extrêmement dangereux (risques de naufrage contre les piles des ponts).</p>

	<p>Ces radeaux pouvaient éventuellement transporter des sacs de charbon depuis leurs lieux de production en forêt.</p>

	<p>Le transport par eau fait l’objet d’une fiscalité féroce. Chaque seigneur dont le château domine le fleuve réclame un droit de péage (le tonlieu).</p>
</section>

