<section>
	<h2>La gestion des forêts</h2>

	<p>Contrairement à l’idée reçue d’un Moyen Âge pillant ses forêts de manière anarchique, les seigneuries ont très tôt mis en place une gestion sylvicole ultra-rigoureuse pour garantir la repousse. On utilise principalement la technique du taillis sous futaie.</p>

	<h3>La technique du taillis sous futaie</h3>

	<p>On divise la forêt en deux niveaux :</p>

	<ul>
		<li><b>Le taillis (le sol) :</b> on coupe les arbres (hêtre, châtaignier, saule) au ras du sol. La souche va alors produire de nombreux rejets verticaux très droits. Ce bois pousse vite et fournit le bois de chauffage et le charbon de bois.</li>

		<li><b>La futaie (les réserves) :</b> on laisse quelques beaux arbres grandir librement au-dessus du taillis pour qu’ils deviennent de gigantesques troncs destinés aux charpentes et à la marine.</li>
	</ul>

	<h3>Rendement et repousse</h3>

	<p>Pour le <b>taillis</b>, la forêt est découpée en parcelles géométriques. Chaque année, le seigneur autorise la coupe d’une seule parcelle, selon un roulement strict.</p>

