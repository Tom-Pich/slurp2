	<p>Les usages domestiques sont : la cuisine, le chauffage et le petit artisanat local (le four du boulanger et le forgeron local).</p>

	<p class="fw-700">Consommation moyenne annuelle par habitant :</p>
	<ul>
		<li><b>En région méditerranéenne :</b> environ <b>1 m³</b>.</li>
		<li><b>En climat tempéré</b>, comme le bassin parisien ou l’Angleterre : <b>2-3 m³</b>.</li>
		<li><b>En climat froid ou montagneux :</b>, cette consommation monte facilement à <b>4-5 m³</b>.</li>
	</ul>
