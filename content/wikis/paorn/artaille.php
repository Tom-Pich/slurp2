<!-- Politique & organisation -->
<section>
	<h2>Politique &amp; organisation</h2>
	<p>L'Artaille est tenue par le prince Sémilis d'Elmour, un gentilhomme distingué au visage altier et aux tempes grisonnantes. Il aime déclamer des poésies sur les remparts de son palais d'Imégie. Les Artaillais le craignent car il a la réputation d’être dur, voire cruel. Certains sont convaincus que Sémilis est fou à lier, mais personne n'a encore réussi à l’éliminer. Le gouverneur, en plus d'être légèrement paranoïaque, semble avoir un véritable don pour déjouer les complats, Sans doute dispose-t-il d'une police secrète très efficace.</p>
</section>

