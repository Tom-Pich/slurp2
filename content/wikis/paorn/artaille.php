<p>L’Artaille est la province la plus méridionnale de la Burgonie. Avec ses immenses prairies, ses vallées verdoyantes, ses collines ombragées et ses forêts giboyeuses, l'Artaille est une terre hospitalière où la vie peut être assez douce.</p>

<!-- Géographie -->
<section>
	<h2>Géographie</h2>
	<p><b>Types de terrain&nbsp;:</b> Plaines fertiles au nord, pâturages au sud, forêts ombragées, collines douces à la frontière avec l'Althusia.</p>
	<p><b>Climat&nbsp;:</b> méditerranéen. Écarts de températures allant de -3° (saison froide) à -40° (saison chaude). Précipitations moyennes.</p>
	
	<!-- Villes -->
	<details>
		<summary>
			<h3>Villes</h3>
		</summary>
		<ul>
		
			<li>
				<b><a href="/wiki/paorn/imegie">Imégie</a>&nbsp;:</b> La capitale provinciale est une cité paresseuse aux maisons blanchies la chaux, installée sur la côte. Le château du gouverneur, une grosse bâtisse aux tours rondes, est édifié sur une colline proche.<br>
				L'université locale, l'École d'Olpalia, est la plus fameuse de Burgonnie méridionale.
			</li>
		
			<li><b><a href="/wiki/paorn/almisie">Almisie</a>&nbsp;:</b> Le centre industrieux de l’Artaille. En plus de cristalleries réputées, on y trouve des tisserands, des forgerons et les meilleurs négociants en vins de Paorn. Détail typique de la province méridionale&nbsp;: malgré sa relative richesse, Almisie ne possède pas de murs d'enceinte, Ses troupes sont cependant largement suffisantes pour assurer sa sécurité contre d'éventuels pillards.</li>
		
			<li><b><a href="/wiki/paorn/stomilie">Stomilie</a>&nbsp;:</b> la citée sainte de l’Atrimisme. Construite sur une colline, la ville est dominée par une cathédrale fortifiée. Bien qu’officiellement sous le contrôle du Prince, le clergé qui dirige la ville jouit d’une relative indépendance par rapport au pouvoir séculaire.</li>
		
			<li><b>Maylie&nbsp;:</b> Le grand port commercial de Burgonnie. C’est de là que partent les nefs marchandes à destination de la Lauria, des îles du Tarot et de Majistra, loin à l'est. Les pirates, qui ont sévi de tous temps dans ce secteur, ont obligé les Maylars (habitants de Maylie) à constituer une flotte de guerre composée d'impressionnantes galères.</li>
		</ul>
	</details>
	
	<!-- Bourgs & villages -->
	<details>
		<summary>
			<h3>Bourgs &amp; villages</h3>
		</summary>
		<ul>
			<li><b>Barlesso&nbsp;:</b> bourg minier situé à 25 km d’Imégie, dans les piémonts des Thundraks. Sinistre et poussiéreux, avec des habitations mal entretenues. Lac aux eaux de couleur rougeâtre, due à l’extraction minière dans la région. Présence de ruines dans les montagnes alentours, réputées hantées. Grande auberge du di <i>Chien sauvage</i>. Parmi ses videurs, on trouve un étonnant demi-orc albinos à l’allure imposante.</li>
			<li><b>Forli&nbsp;:</b> petit village à 5 km d’Almisie. Vignes, céréales, vaches et moutons, culture maraîchère.</li>
			<li><b>Capria&nbsp;:</b> bourg agricole à 20 km d’Almisie. C’est à cet endroit que bifurquent les routes Almisie - Imégie et Almisie - Stomilie.</li>
			<li><b>Beaulieu&nbsp;:</b> petit hameau de pêcheurs, sur la côte à quelques kilomètres au nord d’Imégie.</li>
		</ul>
	</details>
	
	<!-- Routes & distances -->
	<details>
		<summary>
			<h3>Routes &amp; distances</h3>
		</summary>
		<ul>
			<li><b>Imégie – Almisie&nbsp;:</b> 150 km. La route passe par le village de <i>Barlesso</i>. Un chariot ou un piéton met 5 à 6 jours pour faire ce trajet.</li>
			<li><b>Imégie – Almisie, par le chemin des ermites</b> itinéraire un peu plus direct, mais empruntable qu’à pied ou à cheval&nbsp;: 130 km. Cet itinéraire à la réputation d’être un peu moins sûr que le premier – on y voit de temps en temps de créatures dangereuses, et il faut traverser une étrange zone à fluide nul de plusieurs kilomètres de long. Enfin, il y a un peu plus d’une quarantaine de kilomètres à parcourir sans rencontrer de village, ce qui implique, si on se déplace à pied, de passer une nuit à la belle étoile. Avec un bon cheval, il est faisable en deux jours et demi. Il quitte l’itinéraire principal à Barlesso et le rejoint à Capria.</li>
			<li><b>Imégie – Stomilie&nbsp;:</b> en passant par Barlesso, 110 km. La route est carrosable.</li>
			<li><b>Almisie – Stomilie&nbsp;:</b> 130 km.</li>
			<li><b>Almisie – Maylie&nbsp;:</b> 180 km.</li>
		</ul>
		<p>Dans la zone entre Stomilie, Imégie et Almisie, la population est dense. On rencontre un village tous les 10 à 15 km sur les axes routiers. Il est donc très facile, lors d’un voyage de faire une halte dans une auberge chaque soir.</p>
		<p>La route allant d’Almisie à Maylie traverse des régions plus sauvages, à la population plus éparse.</p>
	</details>
	
	<!-- Sites remarquables -->
	<details>
		<summary>
			<h3>Sites remarquables</h3>
		</summary>
		
		<ul>
			<li><b>Les collines d'Althus&nbsp;:</b> Frontière naturelle séparant la Burgonnie de l'Althusia, ces collinees sont garnies de fortins et bourrées de pièges visant à empêcher d'éventuelles agressions venant du sud.</li>
		
			<li><b>La plaine d'Émeraude&nbsp;:</b> Cette immense plaine du nord de l'Artaile est semée de villages et de « châteaux de plaisance » où viennent se délasser les seigneurs du Verbleau.</li>
		
			<li><b>La nécropole d'Ophilie&nbsp;:</b> Situé à la frontière avec le Salthar, cet immense cimetière antique aux tombes étranges est censé être hanté. Des miliers d'aventuriers s'y sont rendus, appâtés par la perspective de trésors hypothétiques, très peu sont revenus.</li>
		</ul>
	</details>
