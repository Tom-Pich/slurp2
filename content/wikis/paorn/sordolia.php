		<div class="has-right-block">
			<p>Avec ses quelque 40 000 km², la forêt Rêveuse est la plus grande forêt de Sordolia — et sans doute la moins connue. Elle s’étend depuis les rivages méridionaux jusqu’au pied du Dernier Rempart, large parfois de cent cinquante kilomètres, rétrécissant en d’autres endroits selon le relief. Un seul axe humain la traverse : la grande route nord-sud reliant la côté à Solidam et passant par Sardam, la capitale. Le long de cette route de 325 km s’égrènent quelques villages distants de trente à cinquante kilomètres les uns des autres. Au-delà de ce couloir, c’est <i>terra incognita</i>. La forêt rêve depuis bien avant que les hommes n’y tracent leurs chemins. Ceux qui s’y aventurent au-delà de la route rapportent tous la même chose : une lumière qui change trop vite, des sons qui portent à des distances impossibles, et le sentiment persistant d’être regardé par quelque chose qui n’a pas besoin de se montrer.</p>

