<!-- Habitants -->
<section>
	<h2>Habitants</h2>
	<p>Les habitants de cette région sont plutôt rudes et éprouvent un profond respect pour la nature, bien que les mœurs des habitants des cités côtières soient en peu plus raffinées.</p>
	<p>Les Sordoliens sont grands et ont une pilosité plutôt claire. Ce sont des gens droits, honnêtes, travailleurs et, malgré leur rudesse, prompts à aider leur prochain.</p>
	<p>Quelle que soit leur profession, tous ont appris à connaître les mystères et les dangers des forêts. Le maniement des armes – particulièrement de la hache – est enseigné aux enfants dès leur plus jeune âge, leur permettant de développer une adresse et une vaillance remarquables.</p>
</section>

<!-- Politique & organisation -->
<section>
	<h2>Politique &amp; organisation</h2>
	<p>La Sordolia est dirigée par le prince Margor d’Avrelanche, un solide guerrier mesurant près de 2 mètres, assisté par son fils. Il vit dans un immense château fort aux murs de granit à Sardam. Homme pragmatique à la franchise légendaire, il est très respecté par ses sujets qui voient en lui un véritable « homme des bois ». Malgré son apparence, Margor joue de l’épinette en virtuose et sait se montrer fin politique quand les circonstances l’exigent.</p>
</section>

<!-- Économie -->
<section>
	<h2>Économie</h2>

	<p>La Sordolia est peu développée. Le climat assez rude rend l’agriculture relativement peu productive. Les zones de pâturages ne sont pas fréquentes et pas toujours sûres. La région jouit par contre d’une faune à fourrure variée et abondante ainsi que d’essences de bois précieux. </p>

	<p>Cette province exporte surtout de la viande, du bois, de la fourrure (castor, hermine de Komashkaïda et renard blanc), de la bière et des meubles qui se vendent à prix d’or dans tout Paorn.</p>

	<p>Les colonies naines qui se sont installées aux abords de la chaîne des Sinistérias produisent des métaux précieux (argent et or) et des gemmes rares.</p>

