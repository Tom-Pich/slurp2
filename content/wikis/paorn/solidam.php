		<h4>Galver, Scrutiner de l’Arcania</h4>

		<p>Grand et svelte, Galver est l'incarnation même de l'autorité froide de l'Arcania. Son crâne entièrement rasé porte l'étoile à sept branches tatouée au front. Sa démarche est mesurée, presque mécanique, comme si chaque pas était le résultat d'un calcul précis.</p>
		<p>Vêtu d'une robe bleue profond aux bordures argentées, il ne porte aucun bijou hormis l'anneau réglementaire des mages déclarés à son index droit - un cercle d'argent gravé de runes subtiles qui luisent faiblement dans la pénombre. Son visage anguleux aux pommettes saillantes semble taillé dans la pierre, dominé par des yeux gris qui observent son environnement avec un mélange de détachement et de vigilance perpétuelle.</p>
		<p>À quarante-trois ans, Galver a servi l'Arcania pendant presque deux décennies. Originaire d'une famille marchande de Lardam, il fut envoyé à Solidam il y a sept ans - une affectation considérée comme un exil par beaucoup de ses collègues, mais qu'il a acceptée sans broncher, fidèle à sa réputation d'obéissance irréprochable.</p>
		<p>Le Mage Galver ne sourit jamais - effet secondaire, disent certains, de la puissante Didascalie qui le contraint à une honnêteté absolue. Cette absence d'expression renforce l'aura d'intimidation qui l'entoure, faisant baisser les yeux aux passants sur son chemin.</p>

		<h4>Les locaux de l'Arcania à Solidam</h4>

		<p>Les locaux de l'Arcania se trouvent dans une annexe du château du comte de Solidam. Il s'agit d'une petite tour en pierre grise à trois étages, reliée au bâtiment principal par un court passage couvert. Son architecture austère et robuste s'intègre parfaitement au style défensif de cette cité-garnison.</p>

		<h5>Rez-de-chaussée</h5>
