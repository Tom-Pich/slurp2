<p>Solidam est une ville garnison, dernier avant-poste humain avant les grandes étendues nordiques et les Sinistérias. La ville, fortifiée par des hautes murailles, compte environ 3000 habitants, ainsi qu’un contingent de 800 soldats de la garde impériale.</p>
<p class="mt-1 ta-center">Devise de la ville : <i>Dernier Bastion, Première Défense</i></p>

<!-- Géographie -->
<section>
	<h2>Géographie</h2>

	<p>Solidam s’étend sur environ 27 hectares</p>

	<p>La ville se situe sur la rive est du fleuve <i>Sigherd</i>, qui fait un peu plus de 50 m de large à hauteur de la ville. À cause de ses courants tourbillonnants, le fond du fleuve est changeant et la navigation est difficile. Seules de petites barques de pêcheurs s’aventurent sur ses eaux troubles.</p>

	<p>Le Sigherd est la jonction entre la rivière appelée <i>Bjergene Magt</i>, qui remonte loin au nord-est, dans le Sinistérias, et une autre rivière {<i>nom à définir</i>}.</p>

	<p>À l’exception notable du château du comte, de l’église et de ses bâtiments attenant, l’essentiel des habitations sont des chaumières à un ou deux étages.</p>

	<details open>
		<summary>
			<h3>Portes de la ville</h3>
		</summary>
		<p>La ville compte trois accès : la porte sud, la porte nord et la porte des pêcheurs.</p>

		<ul class="flow">
			<li><b>La porte nord</b> communique avec une cour entourée de murs haut, sorte d’antichambre donnant accès au château et au reste de la ville. Elle n’est que très peu utilisée et est toujours fermée.</li>
			<li><b>La porte des pêcheurs</b>, au sud ouest, donne accès directement au fleuve et à l’embarcadère. Elle reste généralement ouverte dans la journée.</li>
			<li><b>La porte sud</b>, se trouvant en fait au sud-est, est la porte principale de la ville. C’est par cette porte qu’entrent les caravanes quotidiennes de marchand apportant le ravitaillement nécessaire à la ville.</li>
		</ul>
	</details>

	<details open>
		<summary>
			<h3>Quartiers &amp; secteurs</h3>
		</summary>

		<ul class="flow">
			<li><b>Le château du comte :</b> forteresse d’une taille démesurée, elle sert également de résidence aux troupes impériales.</li>
			<li><b>Le quartier clérical :</b> il comprend l’église Saint Mathys, que les habitants appellent la cathédrale bien que ça n’en soit pas une. Attenantes à l’église, des fortifications délimitent le quartier. À l’intérieur, on y trouve la résidence du grand-prêtre, l’administration ainsi que la caserne des paladins (cinq à six paladins sont présents dans la ville en permanence).</li>
			<li><b>La place de l’église :</b> malgré sa relative étroitesse, c’est ici que se déroule le marché quotidien.</li>
			<li><b>Le quartier marchand :</b> situé immédiatement à la porte sud de la ville. On y trouve la Halle de la ville ainsi que trois des quatres auberges.</li>
			<li><b>le quartier des artisans :</b> situé au nord ouest de la ville</li>
			<li><b>le quartier des pêcheurs :</b> contrairement à ce que son nom indique, il n’y a plus beaucoup de pêcheurs qui vivent ici. Il s’agit du quartier pauvre et mal famé, où l’on trouve notamment beaucoup de prostituées.</li>
			<li><b>Vesterlund, le quartier riche :</b> situé ou nord-est de la ville, séparé de la partie principal par le quartier clérical et la château.</li>
			<li><b>Le hameau, aussi appelé Fiskerley :</b> il s’agit d’un ensemble de chaumières délabrées situé hors les murs, au sud ouest de la ville. C’est là que réside la centaine de pêcheurs et leur famille.</li>
		</ul>
	</details>

</section>

