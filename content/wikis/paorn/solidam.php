			<h3>Quartiers &amp; secteurs</h3>
		</summary>

		<ul class="flow">
			<li><b>Le château du comte :</b> forteresse d’une taille démesurée, elle sert également de résidence aux troupes impériales.</li>
			<li><b>Le quartier clérical :</b> il comprend l’église Saint Mathys, que les habitants appellent la cathédrale bien que ça n’en soit pas une. Attenantes à l’église, des fortifications délimitent le quartier. À l’intérieur, on y trouve la résidence du grand-prêtre, l’administration ainsi que la caserne des paladins (cinq à six paladins sont présents dans la ville en permanence).</li>
			<li><b>La place de l’église :</b> malgré sa relative étroitesse, c’est ici que se déroule le marché quotidien.</li>
			<li><b>Le quartier marchand :</b> situé immédiatement à la porte sud de la ville. On y trouve la Halle de la ville ainsi que trois des quatres auberges.</li>
			<li><b>le quartier des artisans :</b> situé au nord ouest de la ville</li>
			<li><b>le quartier des pêcheurs :</b> contrairement à ce que son nom indique, il n’y a plus beaucoup de pêcheurs qui vivent ici. Il s’agit du quartier pauvre et mal famé, où l’on trouve notamment beaucoup de prostituées.</li>
			<li><b>Vesterlund, le quartier riche :</b> situé ou nord-est de la ville, séparé de la partie principal par le quartier clérical et la château.</li>
			<li><b>Le hameau, aussi appelé Fiskerley :</b> il s’agit d’un ensemble de chaumières délabrées situé hors les murs, au sud ouest de la ville. C’est là que réside la centaine de pêcheurs et leur famille.</li>
		</ul>
	</details>

	<!-- Le château du comte -->
	<details>
		<summary>
			<h3>Le château du comte</h3>
		</summary>
		<p>Le château du comte de Solidam se dresse majestueusement au cœur de la cité, étonnamment imposant pour une ville de cette taille. Ses remparts massifs en pierre grise s'élèvent à plus de quinze mètres de hauteur, dominant les modestes chaumières qui s'agglutinent à ses pieds. Cette forteresse aux proportions impressionnantes n'est pas le simple caprice d'un seigneur vaniteux, mais une nécessité militaire : elle sert de garnison à un contingent de huit cents soldats impériaux chargés de protéger Solidam et les villages environnants des menaces venues du Nord sauvage.</p>
		<p>L'édifice présente plusieurs tours de guet effilées, couronnées de toits coniques en ardoise sombre. Les murailles épaisses sont percées d'étroites meurtrières, tandis que le donjon central s'élance vers le ciel comme pour défier les nuages nordiques.</p>
		<p>Juste à côté, le clocher de l'église de la ville s'élance avec la même audace, créant un duo architectural saisissant. Ces deux colosses de pierre se dressent côte à côte, sentinelles jumelles veillant sur la cité, leurs silhouettes massives contrastant dramatiquement avec les toits de chaume des habitations environnantes. Vus depuis les collines avoisinantes, ils évoquent deux géants de pierre montant la garde parmi un village de fourmis, symboles indéfectibles de l'autorité militaire et spirituelle qui protègent cette ultime frontière de la civilisation burgonne.</p>
	</details>

	<!-- La cour des marchands -->
	<details>
		<summary>
			<h3>La Cour des marchands</h3>
		</summary>

		<p>À l'extérieur de la porte sud de Solidam s'étend une large esplanade pavée, spécialement aménagée pour gérer le flux constant de marchandises qui entrent et sortent de la cité-garnison. Des bornes en pierre délimitent différentes zones selon la nature et la provenance des chargements. À l'approche des portes, des commis de la garde urbaine, registres en main, dirigent méthodiquement les chariots.</p>
		<p>Les petits producteurs locaux sont dirigés vers des zones de déchargement rapide. Ils vendent directement leurs produits à des intermédiaires ou les transportent vers le marché central, puis quittent généralement la ville avant le soir.</p>
		<p>Les marchands venus de plus loin ou transportant des marchandises de valeur supérieure sont orientés vers une cour pavée à l’intérieur de la ville, adjacente à la muraille sud. Cette <i>Cour des Marchands</i> est une zone sécurisée où les caravanes peuvent :</p>
		<ul>
			<li>Décharger leurs marchandises dans des entrepôts sous surveillance</li>
			<li>Faire reposer et soigner leurs bêtes de trait dans des écuries attenantes</li>
			<li>Stationner leurs chariots dans un espace clos et gardé</li>
			<li>Trouver logement dans une des auberges du quartier maechand</li>
		</ul>
		<p>La Cour des Marchands est placée sous l'autorité d'un officier de la garde et de quatre soldats permanents. Un droit d'entrée est perçu selon la taille du convoi et la valeur estimée des marchandises.</p>
		<p>Chaque matin, les chariots arrivés la veille repartent vers le sud. Les marchands se préparent souvent bien avant l'aube, leurs préparatifs éclairés par des lanternes.</p>
	</details>
