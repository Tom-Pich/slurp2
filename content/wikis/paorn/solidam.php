<p>Solidam est une ville garnison, dernier avant-poste humain avant les grandes étendues nordiques et les Sinistérias. La ville, fortifiée par des hautes murailles, compte environ 3000 habitants.</p>

<!-- Géographie -->
<section>
	<h2>Géographie</h2>

	<p>Solidam se situe sur la rive est du fleuve Sigherd, qui fait un peu plus de 50 m de large à hauteur de la ville. À cause de ses courants tourbillonnants, le fond du fleuve est changeant et la navigation est difficile. Seules de petites barques de pêcheurs s’aventurent sur ses eaux troubles.</p>

	<p>Les bâtiments de la ville sont des chaumières de pierre et de bois. Quelques bâtiments sont entièrement en pierre avec toit en ardoise ou en cuivre (le temple d’Alba, la forteresse du Comte Savelmond de Salidam, ainsi que le « château » d’Isandir).</p>

	<h3>Quartiers</h3>
	<p>La ville est divisée en 4 quartiers : le <b>quartier marchand</b> à la porte sud, le <b>quartier noble</b> au centre-nord, le<b> quartier des artisans et des soldats</b> au nord et le <b>quartier des pêcheurs et artisanats « sales »</b> à l’ouest. Ce dernier quartier est pauvre et relativement mal famé. Elle compte trois accès : la porte sud, la porte nord et la porte des pêcheurs.</p>
</section>

<!-- Politique -->
<section>
	<h2>Politique &amp; organisation</h2>

	<p>Le Comte Savelmond de Salidam dirige la ville de manière martiale. Il est assisté par son conseiller principal, Édevan de Sioril, mage puissant (Magerie 4, spécialisé en <i>Emprise mentale</i> et en <i>Connaissance</i>).</p>
</section>

<!-- Économie -->
<section>
	<h2>Économie</h2>
	<p>L’économie de la ville est liée aux troupes impériales (800 hommes) stationnées dans la ville en permanence : gestion de l’import de nourriture (de Sardam), prostituées, armuriers, commerce de minerais avec les nains.</p>

	<p>Les ressources secondaires sont la pêche, la chasse, l’agriculture (insuffisante pour couvrir les besoins de la ville), l’élevage.</p>
</section>

