<p>Le calendrier de l'Empire Burgon est fondé sur les trois âges de l'histoire du monde, tels que définis par la tradition atrimiste. L'année commence au premier jour du printemps.</p>

<!-- Les trois âges -->
<section>
	<h2>Les trois âges</h2>
	<p>L'histoire de Paorn est divisée en trois grandes périodes.</p>
	<table class="border left-1 left-2 left-3">
		<tr>
			<th>Âge</th>
			<th>Période</th>
			<th>Événement fondateur</th>
		</tr>
		<tr>
			<td><b>Premier Âge</b></td>
			<td>Durée inconnue</td>
			<td>De l'origine du monde jusqu'au Cataclysme</td>
		</tr>
		<tr>
			<td><b>Deuxième Âge</b></td>
			<td>~1000 ans</td>
			<td>Du Cataclysme jusqu'à la mort du prophète Eldar</td>
		</tr>
		<tr>
			<td><b>Troisième Âge</b></td>
			<td>En cours</td>
			<td>À partir de la mort du prophète Eldar</td>
		</tr>
	</table>
	<p>Le Troisième Âge a commencé il y a 1490 ans. L'année courante est donc <b>1490 du Troisième Âge</b>, noté <b>1490 T.A.</b> dans les textes officiels. Dans l'usage courant, on dit simplement <i>« l'an 1490 »</i>.</p>
</section>

