<h1>Le fort de Leck</h1>

<div class="has-right-block">

	<p>Le fort de Leck est un des petits forts qui se trouvent le long du <i>Dernier Rempart</i>. Comptant un peu plus de 30 occupants, il est typique des forts que l’on trouve à intervales réguliers sur le rempart.</p>

	<p>Construit en pierres noires, le fort de Leck est d’une grande austérité. Les bannières qui flottent mollement au sommet des tours ne parviennent pas à égayer ce tableau désolé. Leck n’a pas été bâti pour être accueillant - il est là pour tenir, endurant et implacable face aux dangers qui rôdent dans les terres sauvages.</p>

