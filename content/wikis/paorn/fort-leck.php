<h2>Habitants du Fort</h2>
<ul>
	<li>commandant en chef (Hansper)</li>
	<li>deux lieutenants (Berengar, Albrecht)</li>
	<li>24 soldats</li>
	<li>forgeron</li>
	<li>menuisier</li>
	<li>marchel-ferrand</li>
	<li>cuisinier + 2 aides</li>
	<li>prêtre atrimiste (père Dorian, aumônier)</li>
</ul>

<p>Le personnel est relevé tous les 6 mois, au printemps et à l’automne. Tous les occupants du fort sont là volontairement. Les conditions de vie sont difficiles, mais la solde est bonne.</p>

