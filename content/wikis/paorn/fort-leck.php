<h3>Père Dorian, aumônier du fort</h3>
<p>Jeune prêtre d’à peine 30 ans. Fluet, cheveux mi-longs raides blonds, glabre, yeux bleus, mince, assez grande taille, apparence agréable, attitude distante, expression mélant préoccupation et tristesse sur son visage.</p>
<div class="admin flow">
	<p>Son frère aîné était soldat dans la garde du rempart, mais il est mort lors d’une mission au-delà des murs. Dorian souhaite donc contribuer à sa manière à la mission de protection de la garde du Rempart.</p>

	<p>Pouvoirs : <i>Lecture d’Aura</i>, <i>Perception des émotions</i>, <i>Faveur divine</i>, <i>Guérison</i>, <i>Guérison des maladies</i>, <i>Neutralisation du poison</i></p>
</div>

<h3>Berengar, premier lieutenant</h3>
<p>Taille moyenne, présence imposante, une quarantaine d’années. Visage buriné, porte une barbe brune soigneusement taillée, traversée de quelques fils argentés. Longue cicatrice pâle sur joue droite. Yeux gris, toujours en mouvement, scrutent constamment l’horizon avec la vigilance d’un homme qui a appris à ne jamais baisser sa garde.</p>
<p>Armure de cuir bouilli renforcée de plaques métalliques aux épaules et à la poitrine, arborant l’emblème du Dernier Rempart – une tour stylisée sur fond de montagnes. Cape épaisse en laine teinte en gris-bleu. À sa ceinture : épée longue et cor d’alerte.</p>
<div class="admin">
	<p>Vétéran respecté, servant sur le Rempart depuis dix-huit ans. Voix grave et calme qui rassure ses hommes, mais peut devenir tranchante comme l’acier quand la situation l’exige. Il dirige ses patrouilles avec efficacité, privilégiant la prudence et l’observation à la confrontation directe.</p>
	<p>Homme pragmatique et méthodique, Berengar est un leader naturel qui inspire respect et confiance. Sa longue expérience l’a rendu prudent sans être timoré. Il évalue chaque situation avec un calme remarquable, même dans l’urgence. Plutôt taciturne, il ne parle que lorsqu’il a quelque chose d’important à dire, mais sait être chaleureux avec ses hommes. Il considère la garde du Rempart comme une vocation sacrée plus que comme un simple métier, et cette conviction transparaît dans sa façon d’aborder chaque mission. Sa loyauté envers Hansper et ses hommes est inébranlable. Berengar est méfiant envers les inconnus, mais juste et capable de réviser son jugement quand on lui prouve sa valeur.</p>
</div>

<h3>Arthénius, archer d’élite</h3>
<p>Grand et mince, 32 ans, traits fins et au regard perçant. Cheveux blonds coupés courts, barbe de trois jours, mâchoire anguleuse. Nez aquilin avec légère déformation, résultat d’une fracture mal soignée. Porte une armure légère et un arc composite. Six flèches à empennage rouge sont toujours accessibles dans un carquois latéral pour une utilisation immédiate, le reste étant rangé dans un étui fermé. Une dague effilée complète son équipement.</p>
<p class="admin">Observateur né, Arthénius possède un sens aigu du détail qui en fait un éclaireur et un chasseur redoutable. Plutôt solitaire et peu bavard dans son service, il peut devenir étonnamment loquace autour d’un feu de camp, partageant des histoires de chasses ou d’observations de la faune sauvage. Il voue un profond respect à la nature, ce qui lui donne parfois une attitude distante envers ceux qu’il juge irrespectueux de l’environnement. Sa patience légendaire lui permet de rester immobile pendant des heures, guettant une proie ou une menace. Fier de ses compétences sans être arrogant, il apprécie qu’on reconnaisse son talent mais n’en fait jamais étalage.</p>

<h3>Barthold, vétéran endurci</h3>
<p>Colosse de 45 ans. Crâne rasé constellé de petites cicatrices, et son œil gauche, d’un bleu laiteux, est aveugle depuis une escarmouche avec des trolls. Sa barbe fournie, tressée en deux nattes. Porte une cotte de mailles complète. Hache de bataille à deux mains et bouclier rond. Plusieurs amulettes et talismans sont accrochés à son armure.</p>
<p class="admin">Derrière son apparence intimidante se cache un homme étonnamment réfléchi et spirituel. Son expérience des combats l’a rendu fataliste mais pas défaitiste. Il croit aux présages et aux superstitions, d’où ses nombreux talismans. Sa voix profonde et calme raconte souvent des histoires aux jeunes recrues, mêlant conseils pratiques et légendes de la Sordolia. La perte de son œil l’a rendu plus conscient de la fragilité de la vie, développant en lui une patience et une sagesse qui contrastent avec sa férocité au combat. Loyal jusqu’à la mort, il considère ses frères d’armes comme sa véritable famille et se montrera particulièrement protecteur envers le caporal blessé durant le voyage.</p>

<h3>Markus, éclaireur</h3>
<p>Environ 25 ans, taille moyenne. Visage fin aux pommettes saillantes, cheveux noirs mi-long en queue de cheval. Yeux d’un vert intense. Une légère boiterie trahit une ancienne blessure à la jambe gauche, mais ne l’empêche nullement d’être l’un des meilleurs pisteurs de la garnison. Porte des vêtements en cuir souple aux teintes brunes et vertes. Arbalète légère et de deux poignard, se déplace silencieusement.</p>

<h3>Karlheinz, combattant</h3>
<p>Trapu, une trentaine d’années, visage jovial. Cheveux châtains courts, barbe bien entretenue. Porte une brigandine. Masse et bouclier</p>

<h3>Roeland, combattant</h3>
<p>Cheveux rasés, barbe broussailleuse, yeux bleus, carrure armoire à glace, apparence moyenne, assez intelligent, attitude plutôt agréable. Porte une brigandine, une hache et une arbalète.</p>
<p class="admin">Sous ses airs bourrus et sa carrure impressionnante, Roeland cache un tempérament jovial et un humour parfois grinçant qui allège l’atmosphère dans les moments tendus. Originaire d’un village de bûcherons, il conserve une passion pour l’artisanat du bois et sculpte de petits objets durant ses heures de repos. Bien qu’il paraisse simple au premier abord, il fait preuve d’une intelligence tactique naturelle qui a été remarquée par ses supérieurs. Loyal et travailleur, il n’aime pas les complications inutiles et préfère l’action directe. Sa force extraordinaire est souvent mise à contribution pour les tâches difficiles, ce qu’il accepte sans rechigner. Il voue une admiration sans borne à Barthold qui l’a pris sous son aile à son arrivée au fort.</p>

<h3>Marcellus, caporal</h3>
<p>Cheveux courts chatains, barbe broussailleuse, yeux bleu-vert, très grand, apparence agréable, caractère désagréable. Blessé à la jambe (blessure invalidante). Il ne peut pas marcher sans béquille.</p>
<p class="admin">Frustré par sa blessure qui l’empêche de remplir pleinement ses fonctions, Marcellus est devenu irritable et parfois injustement dur avec ses subordonnés. Autrefois considéré comme l’un des meilleurs combattants du fort, il vit mal ce qu’il perçoit comme une déchéance. Sa mauvaise humeur chronique masque une peur profonde de devenir inutile et d’être renvoyé de la garde. Sous cette carapace de rudesse se trouve un homme intelligent, un excellent stratège qui connaît par cœur les tactiques des orcs et autres menaces. Bien qu’il soit désagréable avec presque tout le monde, il reste profondément loyal envers la mission du Rempart et considère son transfert à Solidam comme un échec personnel. Il ne parle jamais de sa vie avant d’intégrer la garde, mais quelques indices suggèrent qu’il est issu d’une famille noble déchue.</p>