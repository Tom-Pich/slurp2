</div>

<p>Adossé au flanc sud du Dernier Rempart, il est entouré d’un mur d’enceinte d’une hauteur de 7 à 8 m, parcouru par un chemin de ronde. Un grand batiment en L se trouve à gauche de la porte permettant de franchir le rempart. Disposant d’un étage, il abrite les pièces de vie des occupants, ainsi qu’une petite chapelle atrimiste.</p>

<p>De l’autre côté de la porte, un bâtiment plus sommaire sert d’entrepôt, de forge et d’atelier de menuiserie. Il jouxte les écuries abritant 15 chevaux. Deux arbalètes scorpions pointent vers la grande porte. Dans la cour, on trouve un enclos avec quelques animaux (volailles, moutons et porcs).</p>

<p>De part et d’autre de la grande porte, deux tours s’élèvent jusqu’à la hauteur du rempart depuis le bâtiment principal et les entrepôts.</p>

