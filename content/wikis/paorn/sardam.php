<section>
	<h2>Géographie</h2>

	<p>Sardam compte environ 15 000 habitants et s’étend sur une centaine d’hectares. La ville, de forme grossièrement circulaire, mesure entre 1 km et 1,2 km de diamètre. Elle est construite sur un terrain plat, au cœur d’une immense clairière défrichée et agrandie au fil des siècles, entourée de terres agricoles fertiles qui s’étendent sur plusieurs kilomètres autour des remparts.</p>

	<p>Au nord et au nord-est, s’étendent les marais du Tonnerre, principale ressource en eau pour les activités artisanales de la cité. Le seul relief notable est une butte rocheuse de 40 à 50 mètres d’altitude, située au centre de la ville, sur laquelle a été édifié le château du Prince.</p>

	<p>La ville possède une double enceinte sur son flanc nord, résultat d’une extension progressive : de nouveaux quartiers se sont développés entre la muraille originelle et la nouvelle muraille nord. À l’est, les deux parties de la ville fusionnent, l’ancienne muraille ayant été en partie démolie.</p>

	<p>Sardam est accessible par deux portes principales : la porte sud, dite porte de Lardam, et la porte nord, appelée porte de Solidam. La ville se situe à 220 km au sud de Solidam et à 255 km au nord de Lardam, ces distances étant données par la route.</p>

	<p>Enfin, fait notable, Sardam est équipée d’un système de tout-à-l’égout : sa situation géographique, la nature du sol et la gestion des ressources en eau ont rendu un tel aménagement indispensable.</p>

