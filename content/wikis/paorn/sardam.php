	<!-- Les blanchisseuses -->
	<details>
		<summary>
			<h3>Les blanchisseuses</h3>
		</summary>

		<h4>Les lavoirs du Tonnerre</h4>

		<p>Les lavoirs sont situés hors les murs, au nord, à une centaine de mètres de la porte de Solidam, là où le courant des Marais du Tonnerre est le plus accessible. Ce n’est pas un simple bord de marais : au fil des siècles, la ville a aménagé plusieurs bassins maçonnés en granit, couverts par des auvents de bois à charpente légère. Chaque bassin est alimenté par un canal creusé depuis le courant principal, avec une évacuation vers l'aval.</p>

		<p>Il y a trois niveaux de bassins :</p>

		<ul>
			<li>Un bassin de trempage en amont (eau la plus propre)</li>
			<li>Des dalles de battage pour le linge encrassé</li>
			<li>Un bassin de rinçage en aval</li>
		</ul>

		<h4>La corporation</h4>

		<p>Les blanchisseuses forment une corporation reconnue par la ville, qui leur garantit l'exclusivité du service rémunéré et l'accès prioritaire aux lavoirs. Chaque membre a sa dalle attitrée — un droit transmissible, parfois vendu ou hérité. La corporation compte une cinquantaine de femmes actives, plus des apprenties et aides.</p>

		<p>Leur organisation pratique :</p>

		<ul>
			<li>Collecte du linge le matin tôt, deux à trois fois par semaine, dans un périmètre de rues assigné à chaque blanchisseuse</li>
			<li>Transport en hotte ou charrette à bras jusqu'aux lavoirs — un trajet d'un quart d'heure depuis le cœur de la ville</li>
			<li>Restitution le surlendemain, linge plié et séché sur les cordes tendues entre piquets dans le champ attenant aux lavoirs (ou à l'intérieur des maisons en hiver)</li>
		</ul>

		<p>La corporation possède un local dans le quartier nord (entre les deux enceintes) qui sert à la fois de dépôt, de point de collecte et de lieu de réunion. C'est là que les tarifs sont affichés et que les litiges sont arbitrés par la maîtresse-blanchisseuse.</p>

		<h4>Le château : un cas à part</h4>
		<p>Le château ne fait pas appel à la corporation. Il dispose de sa propre buanderie intérieure dans les bâtiments de service, alimentée par des citernes et des chaudières permanentes. Le volume de linge (gardes, domestiques, membres de la cour) est trop important et trop régulier pour externaliser. Mais les pièces de lingerie fine du prince et de sa famille sont parfois confiées à deux ou trois blanchisseuses de confiance de la corporation, choisies par la maîtresse de maison — une marque de prestige et un complément de revenu non négligeable.</p>

		<h4>Le problème de l'hiver</h4>
		<p>Quand les marais gèlent — ce qui arrive régulièrement l’hiver, parfois deux à trois semaines d’affilée — les lavoirs du Tonnerre deviennent inutilisables. La corporation bascule alors sur l'eau des puits et sur un système de cuves chauffées installées dans les caves des maisons des blanchisseuses. L'activité ralentit, les délais s'allongent, les tarifs augmentent légèrement. C'est une période traditionnellement tendue entre les blanchisseuses et leur clientèle.</p>

		<h4>Une tension de fond avec les tanneurs</h4>
		<p>Les tanneries sont installées dans le même secteur nord, sur les mêmes marais. Or les tanneurs utilisent en amont des produits corrosifs (chaux, tanins, urines) qui, si peu de précautions sont prises, dégradent la qualité de l'eau en aval. La ville a édicté des règlements sur les rejets, mais leur respect est inégal. La corporation des blanchisseuses et la guilde des tanneurs se livrent une guerre de voisinage larvée, arbitrée périodiquement par l'administration princière, sans jamais trouver de résolution satisfaisante.</p>

	</details>

