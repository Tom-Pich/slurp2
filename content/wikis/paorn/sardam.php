	<details>
		<summary>
			<h3>Auberge du Magelby</h3>
		</summary>

		<p>Magelby signifie, en Sordolien, « le croisement tranquille ». Un établissement de quartier, ni luxueux ni miteux — le genre d'endroit ou mangent les artisans, les marchands de passage et les sergents de ville après leur service.</p>

		<p>Salle rectangulaire basse de plafond, poutres noircies par la fumée. Le sol est en dalles de granit, usé mais propre. Cinq ou six tables, des bancs. Un comptoir en bois épais derrière lequel officie <b>Edvar</b>, l'aubergiste, un homme rougeaud au tablier taché qui parle fort et rit facilement. Une cheminée large où flambe un feu de bouleau diffuse une chaleur sèche. Aux murs, des bois de cerf et un blason de Sardam en bois peint, passablement écaillé.</p>

		<p>Un chat roux, mascotte de l’auberge, dort la plupart du temps sur le rebord d’une fenêtre.</p>

	</details>

