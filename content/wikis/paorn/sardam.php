		<summary>
			<h3>Approvisionnement en eau</h3>
		</summary>
		<p>Aucune rivière ne passe à proximité de la ville. Mais le sous-sol de Sardam est très humide et l’eau qu’on y trouve est d’une grande pureté. Un léger courant circule en permanence dans les Marais du Tonnerre – sauf en période de gel – et ses eaux sont également propres. Les puits sont donc nombreux dans la ville. En complément des pluies, comme le climat de la région est relativement pluvieux, surtout à l’automne et au printemps, de nombreuses habitations sont pourvues de citernes sousterraines maçonnées.</p>
	</details>

	<!-- Quartiers & secteurs -->
	<details>
		<summary>
			<h3>Quartiers &amp; secteurs</h3>
		</summary>

		<h4>La Pelleterie — Quartier sud, autour de la porte de Lardam</h4>
		<p>Le cœur économique de la ville. Les grandes maisons de granit des marchands-pelletiers s'alignent le long de la rue principale qui mène à la porte de Lardam. Entrepôts aux portes clouées, échoppes spécialisées, auberges pour les négociants de passage. L'odeur du cuir traité y est omniprésente, mais c'est une odeur de richesse — ici on apprête les fourrures, on les coud, on les vend. Les rues sont larges, relativement propres (les commerçants paient la corvée de voirie). Le soir, les tavernes sont fréquentées par des marchands venus de toute la Burgonnie. Ambiance prospère, commerçante, un peu froide.</p>

		<h4>Le Pied-du-Roc — Quartier central, autour de la butte du château</h4>
		<p>Le quartier des administrateurs, des notaires, des officiers du Prince et de leurs familles. Les immeubles sont hauts, solides, avec des bas-reliefs aux encadrements de fenêtres. On y trouve les meilleures auberges de la ville — celles où descendent les visiteurs officiels, les émissaires, les PJ qui arrivent avec une lettre du chancelier. Les rues convergent vers la base de la butte et la montée qui mène au château. Ambiance feutrée, discrète, hiérarchisée. On se salue avec componction.</p>

		<h4>La Croisée — Quartier central-est, là où les deux enceintes fusionnent</h4>
		<p>Le quartier populaire par excellence, né de la jonction entre la vieille ville et l'extension nord. Les rues y sont plus étroites, les maisons mélangent le granit ancien et le colombage plus récent. Il y a des marchés de rue quotidiens, des petits artisans (cordonniers, forgerons, chandeliers), des gargotes bon marché. C'est là que les PJ trouveront de l'information sans avoir à se justifier — un forgeron bavard, une tavernière qui connaît tout le monde. Ambiance vivante, bruyante, populaire mais pas dangereuse.</p>

		<h4>Le Faubourg-du-Tonnerre — Extra-muros, au nord de la porte de Solidam</h4>

		<p>Le Faubourg-du-Tonnerre s'est développé hors les murs, collé contre la muraille nord, sur des terres gagnées de haute lutte sur les marais. C'est le quartier des corps de métiers qui ont besoin de l'eau du Tonnerre : tanneurs, corroyeurs, mégissiers, blanchisseuses. Leur proximité immédiate avec les marais est une nécessité pratique autant qu'un avantage fiscal — hors les murs, les matières premières entrent sans acquitter l'octroi.</p>

		<p>Les maisons sont basses, en bois pour la plupart, certaines surélevées d'une marche ou deux pour se prémunir des crues hivernales. Les fondations reposent sur une terre gagnée sur la tourbe, et quelques murs penchent légèrement, inexorablement tirés vers les marais. Les ruelles sentent le tanin, la lessive et la vase. En été, l'odeur devient franche.</p>

		<p>La garde y est peu présente. C'est un quartier de gens qui travaillent tôt, dorment peu et se méfient des étrangers bien habillés. Il n'est pas vraiment dangereux — la population est laborieuse, pas criminelle — mais on n'y entre généralement pas sans raison.</p>

