	<h2>Politique &amp; organisation</h2>

	<p>La Sordolia – et donc Sardam – est dirigée par le <b>prince Margor d'Avrelanche</b>. Voir l’<a href="/wiki/paorn/maison-avrelanche">article dédié</a>.</p>

	<!-- Garde de la ville -->
	<details>
		<summary>
			<h3>Garde de la ville</h3>
		</summary>

		<p>La garde de la ville est un corps distinct de la garde du château. Elle compte un peu plus d’une cinquantaine d’hommes, complétés par des auxiliaires (guet de nuit assuré en partie par des citadins de corvée).</p>

		<p>Elle est placée sous l'autorité d’un <b>capitaine de ville</b> assisté de trois sergents, qui encadrent directement les hommes de rang. <b>Capitaine Huygen :</b> 50 ans, cheveux gris coupés ras, mâchoire carrée. Un homme de terrain. Il connaît chaque ruelle de la Pelleterie, chaque marchand, chaque ivrogne récidiviste. Vingt-cinq ans dans la garde de ville, capitaine depuis huit. Il fait son travail sans état d'âme et n'apprécie pas qu'on vienne lui compliquer la vie depuis le château.</p>

		<p>Une <b>caserne principale</b> dans le quartier de la Pelleterie à proximité de la porte sud (porte de Lardam) — là où le flux commercial est le plus dense et les risques de trouble les plus élevés. Un <b>poste secondaire</b> près de la porte nord.</p>
