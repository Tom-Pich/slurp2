	<details>
		<summary>
			<h3>Le système d'évacuation de Sardam</h3>
		</summary>

		<h4>Le problème spécifique à la ville</h4>

		<p>La combinaison sous-sol très humide, nappe phréatique pure et terrain plat crée une contrainte sévère : des latrines à fosse banales contamineraient rapidement les puits. C'est précisément ce qui a contraint la ville à développer un système de canaux, plutôt que de s'en remettre aux fosses individuelles.</p>

		<p>Le réseau de caniveaux couverts</p>
		<p>Le réseau repose sur des canaux de granit maçonnés, couverts de dalles amovibles, qui courent sous les rues principales. La pente est infime — le terrain est plat — mais suffisante car la ville a été conçue avec un léger dévers artificiel vers le nord lors de sa fondation : les rues descendent imperceptiblement depuis le centre vers la porte de Solidam.</p>

		<p>Les immeubles et maisons sont raccordés au réseau via des conduits verticaux maçonnés rejoignant le canal de rue. Les latrines collectives des immeubles d'habitation (cuisines, arrière-cours) y déversent directement. C'est le tout-à-l'égout proprement dit.</p>

		<p>Les canaux principaux convergent vers deux collecteurs qui franchissent la muraille nord (sous des arches à vantaux de bois, fermables en cas de siège) et débouchent dans les marais du Tonnerre.</p>

		<h4>Les marais comme filtre naturel</h4>

		<p>C'est l'élément clé du système : les marais ne reçoivent pas les effluents dans le courant principal utilisé par les lavoirs et les tanneries, mais dans des bras périphériques aménagés à l'ouest du courant. La végétation marécageuse (roseaux, joncs, laîches) filtre naturellement une grande partie des matières organiques avant que les eaux ne rejoignent le courant principal — plus à l'est et en aval.</p>

		<p>Cette séparation spatiale est réglementée et entretenue : un cantonnier municipal est chargé de surveiller les débouchés et de signaler tout colmatage ou débordement.</p>

		<h4>Les fosses des particuliers non raccordés</h4>

		<p>Tous les bâtiments ne sont pas raccordés — les maisons modestes des faubourgs (entre les deux enceintes, quartier nord) n'ont souvent qu'une fosse maçonnée étanche dans leur cave ou arrière-cour. L'étanchéité est obligatoire par règlement, précisément pour protéger la nappe.</p>

		<p>Ces fosses sont vidangées périodiquement par les maîtres-vidangeurs, une corporation de nuit dont l'activité est strictement encadrée : travail entre minuit et l'aube, transport en tonneaux fermés par les ruelles secondaires, dépôt dans des fosses de compostage situées hors les murs à l'ouest (loin des marais, loin des puits). Le contenu traité finit sur les champs agricoles environnants — une ressource, pas un déchet.</p>

		<h4>Le château : un système distinct</h4>

		<p>Le château bénéficie de la pente naturelle de la butte : ses latrines (des garderobes suspendues en encorbellement sur les murailles, ou des fosses internes) évacuent directement vers le bas de la roche, dans des canaux taillés dans le granit. Les effluents rejoignent un collecteur qui contourne la butte par l'est et descend vers les marais via un canal extérieur à la ville. Le château ne dépend donc pas du réseau urbain.</p>

		<h4>La surveillance et les règlements</h4>

		<p>La ville dispose d'un inspecteur des eaux et voiries, nommé par le chancelier, dont la charge comprend :</p>

		<ul>
			<li>L'entretien et le curage annuel des canaux (corvée urbaine ou entreprise rémunérée)</li>
			<li>La vérification de l'étanchéité des fosses privées lors de transactions immobilières</li>
			<li>L'arbitrage des plaintes entre voisins (raccordements sauvages, débordements)</li>
			<li>La surveillance de la zone de débouché dans les marais</li>
		</ul>

		<p> Les infractions (fosse non étanche, rejet sauvage dans la rue) sont punies d'amendes. Les récidivistes peuvent se voir contraints aux travaux de curage.</p>

		<h4>Un équilibre fragile</h4>

		<p>Le système fonctionne — mais reste vulnérable. Une crue des marais qui reflue dans les collecteurs, un curage négligé pendant quelques années, une saison de gel prolongée qui fissure les maçonneries&hellip; et des puits commencent à se contaminer. Des épidémies sporadiques de fièvres intestinales rappellent périodiquement aux habitants que la pureté de leur eau n'est pas un acquis.</p>
	</details>


