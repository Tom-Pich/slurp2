<section>
	<h2>Géographie</h2>

	<p>Sardam compte environ 12&thinsp;000 habitants et s’étend sur une centaine d’hectares. La ville, de forme grossièrement circulaire, mesure entre 1 km et 1,2 km de diamètre. Elle est construite sur un terrain plat, au cœur d’une immense clairière (la <i>clairière de Matar</i>) agrandie au fil des siècles, entourée de terres agricoles fertiles qui s’étendent sur plusieurs kilomètres autour des remparts.</p>

	<p>La zone défrichée est traversée selon un axe nord-sud par la <i>Rojen</i>, large rivière navigable dont les eaux, généralement tranquilles, alimentent les <i>marais du Tonnerre</i>. Ces derniers se trouvent au nord et au nord est de l’emplacement de la ville.</p>

	<p>Le seul relief notable est une butte rocheuse située au centre de la ville, sur laquelle a été édifié le <a href="/wiki/paorn/chateau-sardam">château du Prince</a>.</p>

	<!-- Structure de la ville -->
	<details>
		<summary>
			<h3>Structure de la ville</h3>
		</summary>

		<p>La ville possède une double enceinte sur son flanc nord, résultat d’une extension progressive. À l’est, les deux parties de la ville fusionnent, l’ancienne muraille ayant été en partie démolie.</p>

		<p>Sardam est accessible par deux portes principales : la porte sud, dite porte de Lardam, et la porte nord, appelée porte de Solidam. La ville se situe à 220 km au sud de Solidam et à 255 km au nord de Lardam, ces distances étant données par la route.</p>

		<p>Enfin, fait notable, Sardam est équipée d’un système de tout-à-l’égout : sa situation géographique, la nature du sol et la gestion des ressources en eau ont rendu un tel aménagement indispensable.</p>
	</details>

	<!-- Architecture -->
	<details>
		<summary>
			<h3>Architecture &amp; urbanisme</h3>
		</summary>

		<h4>La porte Nord</h4>

		<p>La porte nord de la ville est flanquée de deux grandes tours portant chacune une gravure en haut-relief, représentant un personnage presque aussi haut que la tour.</p>

		<p>La tour de gauche représente <i>Matarcald le Défricheur</i>, ancien roi du Nord et fondateur légendaire de la ville. Un géant de granit sculpté en pied, de face, dans une posture d'autorité immobile. Il tient une hache à deux mains, lame vers le bas. Il ne porte pas d'armure mais une cape épaisse et une couronne simple — presque rustique. Son visage est carré, la barbe taillée en pointe, le regard droit devant lui. Sous ses pieds, une forêt stylisée d'arbres tombés. L'inscription en bas de la tour : <i>Il a offert la terre aux hommes</i> – en sordolien archaïque.</p>

		<p>La tour de droite montre <i>Sendra d'Avrelanche</i>, guerrière et première princesse de Sordolia. Une femme debout, de profil à trois quarts, en armure complète, la main droite levée tenant une épée à la verticale. La posture évoque moins le combat que le serment. Ses cheveux sont tressés, visibles sous un heaume ouvert. Son visage est serein, presque froid. Derrière elle, en bas-relief plus discret, un château en construction. L'inscription : <i>Elle a tenu ce que d'autres n'osaient bâtir</i> – en sordolien archaïque.</p>

	</details>

	<!-- Quartiers & secteurs -->
	<details>
		<summary>
			<h3>Quartiers &amp; secteurs</h3>
		</summary>

		<h4>La Pelleterie — Quartier sud, autour de la porte de Lardam</h4>
		<p>Le cœur économique de la ville. Les grandes maisons de granit s'alignent le long de la rue principale qui mène à la porte de Lardam. Entrepôts aux portes clouées, échoppes spécialisées, auberges pour les négociants de passage. L'odeur du cuir traité y est omniprésente, mais c'est une odeur de richesse — ici on apprête les fourrures, on les coud, on les vend. Les rues sont larges et propres. Le soir, les tavernes sont fréquentées par des marchands venus de toute la Burgonnie. Ambiance prospère, commerçante, un peu froide.</p>

		<h4>Le Pied-du-Roc — Quartier central, autour de la butte du château</h4>
		<p>Le quartier des notables et de leurs familles. Les maisons sont hautes, solides, avec des bas-reliefs aux encadrements de fenêtres. On y trouve les meilleures auberges de la ville — celles où descendent les visiteurs officiels, les émissaires, les riches négociants étrangers. Les rues convergent vers la base de la butte et la montée qui mène au château. Ambiance feutrée, discrète, hiérarchisée. On se salue avec componction.</p>

		<h4>La Croisée — Quartier central-est, là où les deux enceintes fusionnent</h4>
		<p>Le quartier populaire, né de la jonction entre la vieille ville et l’extension nord. Les rues y sont plus étroites, les maisons mélangent le granit ancien et le colombage plus récent. Il y a des marchés de rue quotidiens, des petits artisans, des gargotes bon marché. C'est là que les PJ trouveront de l'information sans avoir à se justifier — un forgeron bavard, une tavernière qui connaît tout le monde. Ambiance vivante, bruyante, populaire mais pas dangereuse.</p>

		<h4>Le Faubourg-du-Tonnerre — Extra-muros, au nord de la porte de Solidam</h4>

		<p>Le Faubourg-du-Tonnerre s'est développé hors les murs, collé contre la muraille nord, sur des terres gagnées de haute lutte sur les marais. C'est le quartier des corps de métiers qui ont besoin d’eau : tanneurs, corroyeurs, mégissiers, blanchisseuses.</p>

		<p>Les maisons sont basses, en bois pour la plupart, surélevées d'une marche ou deux pour se prémunir des crues hivernales. Les fondations reposent sur une terre gagnée sur la tourbe, et quelques murs penchent légèrement, inexorablement tirés vers les marais. Les ruelles sentent le tanin, la lessive et la vase. En été, l'odeur devient franche.</p>

		<p>La garde y est peu présente. C'est un quartier de gens qui travaillent tôt, dorment peu et se méfient des étrangers bien habillés. Il n’est pas vraiment dangereux — la population est laborieuse, pas criminelle — mais on n'y entre généralement pas sans raison.</p>

	</details>

	<!-- Gestion de l’eau -->
	<details>
		<summary>
			<h3>Gestion de l’eau</h3>
		</summary>

		<p>Le sous-sol de Sardam est très humide et l’eau qu’on y trouve est d’une grande pureté – on lui prête même des vertues médicinales. Un léger courant circule en permanence dans les Marais du Tonnerre – sauf en période de gel – et ses eaux sont également propres. Les puits sont donc nombreux dans la ville.</p>

		<p>La combinaison sous-sol très humide, nappe phréatique pure et terrain plat a contraint la ville à développer un système de canaux d’évacuation, plutôt que d’utiliser des fosses individuelles. Le réseau repose sur des canaux de granit maçonnés, couverts de dalles amovibles, qui courent sous les rues principales. La plupart des habitations sont raccordées au réseau via des conduits verticaux maçonnés rejoignant le canal de rue.</p>

		<p>Les canaux convergent vers des collecteurs qui débouchent dans des bras périphériques des marais. La végétation marécageuse (roseaux, joncs, laîches) filtre naturellement une grande partie des matières organiques avant que les eaux ne rejoignent la Rojen.</p>

		<p>Le système fonctionne — mais reste vulnérable. Une crue qui reflue dans les collecteurs, un curage négligé pendant quelques années, une saison de gel prolongée qui fissure les maçonneries… et des puits commencent à se contaminer. Des épidémies sporadiques de fièvres intestinales rappellent périodiquement aux habitants que la pureté de leur eau n'est pas un acquis.</p>
	</details>

