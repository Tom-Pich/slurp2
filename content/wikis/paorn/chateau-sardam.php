<!-- Le commandant Eward Kantrel -->
<details>
	<summary>
		<h3>Eward Kantrel, commandant du château</h3>
	</summary>

	<p>Kantrel est un homme d’une cinquantaine d’années, de stature robuste. <span class="admin">Trente ans de service, capitaine de la garde pendant 10 ans, désormais commandant de la garde du château depuis six ans. Homme respecté, d'origine modeste (fils d'un sergent de village), promu à la force du mérite.</span></p>

	<p>Droiture absolue — ce qui lui vaut autant d'admirateurs que d'ennemis. Il a plusieurs fois refusé des passe-droits à des nobles, dont Meck lui-même (accès au château pour des marchandises non déclarées, il y a deux ans).</p>
</details>

