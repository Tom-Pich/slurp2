	</summary>

	<div class="has-right-block">
		<p>Administrateur des affaires courantes de la province.</p>
		<p>La soixantaine sèche, visage anguleux, encadré par des cheveux gris soigneusement tirés en arrière. Porte une barbe courte, taillée avec précision, et des lunettes cerclées de cuivre qu’il ajuste souvent sur son nez. </p>
		<p>Ancien officier de justice devenu administrateur — il connaît le droit burgon mieux que quiconque en Sordolia. </p>
