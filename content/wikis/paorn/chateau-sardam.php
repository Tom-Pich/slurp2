<h2>Architecture du château</h2>

<p>Le château est divisé en sept zones.</p>

<!-- 1. La cour et les accès -->
<details>
	<summary>
		<h3>1. La cour et les accès</h3>
	</summary>

	<p>La cour intérieure est le poumon du château — jamais vraiment calme, même la nuit. Les bâtiments la cernent de trois côtés ; le quatrième est la grande porte ferrée. Le sol est pavé de granite gris, usé au centre par trois siècles de passage.</p>

	<p>Une odeur permanente de crottin et de paille humide, que le vent du nord dilue les bons jours. Les domestiques circulent vite, tête baissée, chargés de hottes ou de ballots. Les gardes changent de poste avec une ponctualité mécanique. Les chevaux attachés aux anneaux des écuries attendent avec cette patience bovine des bêtes qui ont appris à ne pas s'énerver.</p>

	<p class="italic">Ambiance : activité sourde, fonctionnelle. On ne traîne pas dans la cour sans raison visible.</p>
</details>

<!-- 2. Le corps de logis principal -->
<details>
	<summary>
		<h3>2. Le corps de logis principal</h3>
	</summary>

	<p>Le bâtiment le plus imposant, plusieurs niveaux : grande salle, salle d'audience, appartements nobles.</p>

	<p>Plafonds hauts, fenêtres à vitraux plombés. Les couloirs sont tapissés — chasse, batailles anciennes, animaux brodés sur fond sombre. Les dalles sont recouvertes de nattes tressées qui étouffent les pas.</p>

	<p>Les portes des appartements privés sont fermées. On entend par moments une voix, de la musique (épinette, quelque part au fond), un rire bref. Des pages circulent avec des plateaux ou des messages ; ils s'effacent contre les murs quand un adulte passe.</p>

	<p>La grande salle est vide la plupart du temps — l'immense table de chêne peut accueillir quatre-vingts convives mais ne sert que lors des grandes assemblées. En dehors de ça, un domestique en robe grise cire méthodiquement le plancher dans un coin, et la pièce sent la térébenthine et la cire d'abeille.</p>

	<p class="italic">Ambiance : silence de représentation. Tout ici est conçu pour impressionner — même quand personne ne regarde.</p>

	<!-- Salle d’audience -->
	<details>
		<summary>
			<h4>La salle d'audience (1<sup>er</sup> étage)</h4>
		</summary>
		<p>La pièce où le Prince reçoit officiellement. Pas la grande salle de banquet — celle-ci est plus courte, plus haute, conçue non pour festoyer mais pour impressionner les visiteurs, qui restent debout.</p>
		<p>Le plafond monte à huit mètres. Deux rangées de piliers de granit encadrent l'allée centrale, portant des torchères de fer à hauteur de tête d'homme. Les murs latéraux sont couverts de tapisseries de chasse aux couleurs sombres — sangliers et cerfs dans une forêt stylisée, fond azur. Au fond, la chaise du Prince : pas un trône sculpté, mais un fauteuil de bois massif à dossier haut, sur une estrade de deux marches. Sobre, imposant par sa simplicité même.</p>
		<p>Derrière la chaise, suspendu entre deux pilastres, l'emblème de la maison d'Avrelanche — d'argent à la coulée de roc de sable descendant des Sinistérias, étoile polaire d'or en chef — brodé sur une tenture de velours bleu nuit, grande comme une porte.</p>
		<p>La salle est froide quand elle est vide. Elle ne l'est jamais longtemps : c'est ici que Margor reçoit les délégations, tranche les litiges qui remontent jusqu'à lui, prononce les rares discours que les circonstances exigent. Il y vient à reculons — il préfère les décisions prises autour d'une table que les annonces faites depuis une estrade.</p>
		<p class="italic">Ambiance : solennité froide. L'espace est conçu pour que celui qui entre sente le poids du pouvoir avant même d'avoir parlé.</p>
	</details>

</details>

<!-- 3. Les quartiers de la garde (aile nord-ouest) -->
<details>
	<summary>
		<h3>3. Les quartiers de la garde (aile nord-ouest)</h3>
	</summary>

	<p>170 m<sup>2</sup> au sol,sur deux niveaux. Couloirs larges, plafonds bas, torches dans des anneaux de fer. Les murs sentent la graisse d'arme et la laine mouillée.</p>

	<p class="fw-700">Rez-de-chaussé</p>
	<ul>
		<li>refectoire (la nourriture est amenée depuis les cuisines) qui fait aussi office de salle de réunion ~ 12,5×6 = 75 m²</li>
		<li>armurerie ~ 50 m²</li>
		<li>lattrines ~ 10 m²</li>
		<li>accueil corps de garde. Sergent de permanence, tableau de service et lefs des armoires. ~ 8 m²</li>
		<li>réserve de matériel courant — torches, huile, cordes, seaux — distincte de l'armurerie. Un réduit sous l'escalier ~ 4 m²</li>
		<li>accès à la tour nord-ouest</li>
		<li>murs et accès ~ 20 m²</li>
	</ul>

	<p class="fw-700">1er étage</p>
	<ul>
		<li>chambres des gardes (13 de 6 m²) ~ 80 m²</li>
		<li>chambres des officiers (3 de 12 m²) ~ 36 m²</li>
		<li>bureaux (2 de 12 m²) ~ 24 m²</li>
		<li>accès tour nord-ouest</li>
		<li>lattrines ~ 10 m²</li>
		<li>murs et accès ~ 20 m²</li>
	</ul>

	<p>Le réféctoire est généralement occupé par les gardes en période de repos : tabourets renversés, dés oubliés sur une table, une veste d'uniforme pendue à un crampon. Les hommes parlent fort ou ne parlent pas du tout. Un sergent passe avec un registre sous le bras sans regarder personne. Un tableau de service épinglé par une dague — les rotations de garde, les noms, les heures. Une cible d'entraînement au fond avec des couteaux plantés dedans.</p>

	<p>L'armurerie est fermée à clef, mais on entend derrière la porte le bruit régulier d'une pierre à aiguiser.</p>

	<p>Le bureau d'Ostveig est situé au premier étage : petite pièce de travail de 4×3 m², une table, le registre des rondes. Propre, rangé, rien de superflu.</p>

	<p class="italic">Ambiance : décontraction des gens habitués au danger, attention diffuse mais permanente.</p>

</details>

<!-- 4. L'aile administrative (aile est) -->
<details>
	<summary>
		<h3>4. L'aile administrative (aile est)</h3>
	</summary>
	<p>Trois niveaux. Bâtiment visiblement plus récent. Salle de l'Assemblée au rez-de-chaussée. Bureaux, salle des archives au premier étagès. Logements de fonction au deuxième étage.</p>

	<p>Escalier de pierre étroit, rampe de corde. Les marches sont usées au centre, lisses comme du savon. À chaque palier, une fenêtre à meneaux donne sur la cour ou sur la ville — lumière froide, changeante selon l'heure.</p>

	<p>Les couloirs sont plus silencieux ici. Portes de bois sombre, chacune avec une petite pancarte gravée. L'odeur dominante est celle de l'encre et de la poussière de parchemin, avec par moments un relent de cire à cachets. Des secrétaires traversent d'un bureau à l'autre sans lever les yeux, serrant des liasses contre leur poitrine. Un greffier âgé dort sur sa chaise dans l'embrasure d'une porte ouverte, la plume encore à la main.</p>

	<p class="italic">Ambiance : concentration, hiérarchie silencieuse. On chuchote même quand on n'a rien à cacher.</p>

	<!-- Salle de l’Assemblée provinciale -->
	<details>
		<summary>
			<h4>La salle de l'Assemblée provinciale (rez-de-chaussée du bâtiment nord)</h4>
		</summary>
		<p>Construite bien après le reste du château, au moment où les fiefs sordoliens ont exigé leur représentation formelle. L'architecture le trahit : les murs sont un peu trop droits, la pierre un peu trop claire, les proportions moins organiques que le reste du bâtiment.</p>
		<p>Salle rectangulaire avec des gradins de pierre sur trois côtés — une vingtaine de places par côté, rarement toutes occupées. Au centre, une table en fer à cheval autour de laquelle siègent les représentants pendant les délibérations. Face ouverte du fer à cheval : la chaise du président de séance, un chambellan nommé par le Prince, dont le rôle est de modérer sans trancher.</p>
		<p>Les armes de chaque fief vassal sont gravées dans la pierre au-dessus de leurs places respectives — les Hareld, les Brokkeld, les Krantz, les Vorn, et au centre en plus grand, les Avrelanche. Les armes de Meck, en tant que vassal du duc de Lardam, ne figurent pas ici directement. Il siège dans les gradins, pas autour de la table — un détail protocolaire qu'il n'a jamais digéré.</p>
		<p>La salle ne sert que trois ou quatre fois par an. Entre les sessions, elle est utilisée comme salle de stockage pour les archives de l'Assemblée et comme lieu de réunion informelle pour les secrétaires des différentes délégations. Des parchemins traînent sur la table. Une bougie oubliée a coulé sur le bord du gradin nord et personne ne l'a encore grattée.</p>
		<p class="italic">Ambiance : pouvoir en sommeil. La pièce sent le parchemin et la politique froide.</p>
	</details>

	<!-- Salle des archives -->
	<details>
		<summary>
			<h4>La salle des archives (rez-de-chaussée du bâtiment sud)</h4>
		</summary>

		<p>La porte est en chêne épais avec une serrure à deux points — elle était fermée en l'absence du greffier des archives, Orwald Kreun.</p>

		<p>La salle est voûtée, plus longue que large. L'odeur qui vous accueille est celle du vieux papier, de la cire et d'une légère acidité — de l'encre ancienne, de la poussière dans les reliures. Quatre fenêtres étroites percent le mur est : en ce moment de la journée, elles donnent une lumière froide mais suffisante. Le soir, ce serait une autre affaire.</p>

		<p>Une balustrade de bois divise la salle en deux. D’un côté, des étagères du sol au plafond chargées de registres reliés, classés par année et par service, étiquettes manuscrites sur le dos. De l'autre côté de la balustrade, une cage grillagée fermée à double tour, où s'entassent les archives plus anciennes — rouleaux de parchemin dans des tubes de bois, coffrets plombés, liasses ficelées. Ce côté-là, on n'y entre pas sans autorisation spéciale.</p>

		<p>Au centre, côté fenêtre, le poste de travail de Kreun : un pupitre incliné, haut, avec une tablette pour poser les registres ouverts. Sur le pupitre, un encrier personnel, un chasse-mouche, une loupe sur pied, orientable. Une lampe à huile allumée, même en plein jour. La chaise est rembourrée et usée à la forme de quelqu'un qui s'y assoit huit heures par jour depuis des années.</p>

		<p>Juste au-dessus du pupitre, la tranche des registres d'approvisionnement de la garde — une rangée entière.</p>

	</details>

</details>

<!-- 5. Les cuisines et bâtiments de service (aile sud) -->
<details>
	<summary>
		<h3>5. Les cuisines et bâtiments de service (aile sud)</h3>
	</summary>

	<p>Semi-enterrée côté roche, accessible depuis la cour par une volée de marches descendantes. La chaleur frappe dès l'entrée — deux grandes cheminées tournent en permanence de l'aube à la nuit. Le plafond est bas, voûté de briques noircies. Des crochets de fer partout : volailles pendues, ustensiles, herbes séchées en bottes.</p>

	<p>Vacarme organisé. La cuisinière en chef — une femme de quarante ans au tablier perpétuellement taché — dirige tout ça à mi-voix avec une autorité tranquille. Les marmitons ne courent pas, mais ne s'arrêtent pas non plus. Une petite fenêtre carrée donne dans la cour, à hauteur de chevilles — les pieds des passants défilent devant elle toute la journée.</p>

	<p>Odeurs : viande rôtie, oignons, levain frais. Et, en fond, une légère odeur de drains que l'aération ne parvient pas tout à fait à chasser.</p>

	<p class="italic">Ambiance : chaleur, efficacité, territoire de quelqu'un d'autre. Les PJ y sont poliment tolérés.</p>
</details>

<!-- 6. Chapelle et pièces de service (aile ouest) -->
<details>
	<summary>
		<h3>6. Chapelle et pièces de service (aile ouest)</h3>
	</summary>
	<p>La plus étroite.</p>

	<h4>La chapelle du château (rez-de-chaussée)</h4>
	<p>Petite par rapport à la masse du château — une nef unique, six travées, voûtée en berceau de granit poli. Pas de vitraux : les fenêtres sont étroites, en plein cintre, garnies de cire huilée qui laisse passer une lumière diffuse et laiteuse. La pierre a absorbé des décennies d'encens ; l'odeur est là en permanence, tiède, légèrement sucrée, même quand la chapelle est vide.</p>

	<p>L'autel est sobre — dalle de granit gris, nappe de lin blanc, chandelier à sept branches en fer forgé. Au-dessus, gravé dans la pierre, la croix cerclée : symbole canonique de la foi atrimiste.</p>

	<p>Sur le mur gauche, une plaque de bronze commémore les membres de la maison d'Avrelanche morts au service de la province. Les noms les plus anciens sont presque illisibles, rongés par l'humidité. Les plus récents sont gravés proprement, avec les dates.</p>

	<p>L'aumônier du château, <b>frère Aldgrim</b>, officie ici matin et soir. </p>

	<p>La chapelle est ouverte à toute heure. Certains gardes viennent y faire un arrêt bref avant leur tour de nuit, sans vraiment prier — par habitude, ou par superstition.</p>

	<p class="italic">Ambiance : silence actif, légèrement oppressant. On baisse instinctivement la voix.</p>
</details>

<!-- 7. Les niveaux bas — caves, cellules, passages -->
<details>
	<summary>
		<h3>7. Les niveaux bas — caves, cellules, passages</h3>
	</summary>
	<p>Taillés dans la roche vive de la butte. On y descend par un escalier à vis dont les marches n'ont pas été retaillées depuis la fondation du château — inégales, usées de travers. L'air change dès le premier palier : froid, humide, avec une odeur de vieille pierre et de rouille.</p>

	<p>Les caves d'approvisionnement occupent le plus gros de l'espace : barriques, coffres, étals de sel. Des rats. La réserve d'huile pour les torches est ici, dans des jarres de terre scellées à la cire.</p>

	<p>Plus loin, séparées par un couloir étroit dont la torche de garde enfume le plafond noir, se trouvent les cellules. Trois portes de fer avec judas. Deux sont vides. La troisième, au fond, est celle de Kantrel.</p>

	<p class="italic">Ambiance : le château cesse d'être une demeure et redevient une forteresse.</p>
</details>

