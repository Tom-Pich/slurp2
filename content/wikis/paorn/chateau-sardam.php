		<p>Voix basse et précise, jamais un mot de trop.</p>
		<p>Vêtu d’une robe de fonction sobre, bleu nuit, rehaussée d’un collier d’argent portant le sceau du Prince. Porte une chevalière en fer (pas en or) qu'il fait tourner autour de son doigt quand il réfléchit.</p>
		<p class="admin"><b>Relation avec Margor :</b> respect mutuel, pas de l'amitié. Margor est un homme d'instinct et de franc-parler ; Vasshen est un homme de dossiers et de nuances. Le prince le surnomme « mon notaire » — mi-moqueur, mi-admiratif. Vasshen encaisse sans broncher. En privé, il considère que Margor est le meilleur prince que la Sordolia puisse avoir, <i>à condition qu'on l'empêche de résoudre les problèmes politiques à la hache</i>. C'est exactement son rôle.</p>
