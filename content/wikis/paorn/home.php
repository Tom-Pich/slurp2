<h3>La Burgonnie</h3>
<p>Un brin de Robin des bois, quelques assassinats dignes de la Renaissance, des intrigues entre nobles à n'en plus pouvoir&hellip; La Burgonnie est le plus «&nbsp;classique&nbsp;» des empires.</p>
<h3>Le Salthar</h3>
<p>Plus «&nbsp;fantasy&nbsp;» que la Burgonnie, le Salthar est un royaume de montagnes qui n'a rien à envier à la Transylvanie. Espions se faufilant dans des ruelles embrumées, lutins et dryades vivant dans des forêts sans fond, sommets tourmentés et majestueux déchirant les nuages épais. Le grandiose et l'étrange peuvent surgir au milieu de brumes qui semblaient pourtant ordinaires.</p>
<h3>La Tsovranie</h3>
<p>Le froid, la glace dans les cœurs, le souffle court, l'aventurier tsovranien doit lutter contre les éléments. Ce pays semble plus «&nbsp;normal&nbsp;» que les autres, bien qu'un peu désolé. Mais cela laisse place aux intrigues de palais ou aux courses dans les steppes.</p>
<h3>L'Althusia</h3>
<p>Oracles, astrologues, poisons, une civilisation à mi-chemin entre celle des Aztèques et des anciens Égyptiens&hellip; tout semble dit. Mais il y a aussi le Pays noir et ses mystères.</p>
