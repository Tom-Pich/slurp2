<h1>Fallenbeck, gros village au nord de Sardam</h1>

<p>Gros village fortifié de 800 habitants environ, situé sur la route <a href="/wiki/paorn/sardam">Sardam</a>–<a href="/wiki/paorn/solidam">Solidam</a>, à 27 km au nord de Sardam. Halte incontournable pour les convois de ravitaillement à destination de Solidam, Fallenbeck doit sa prospérité relative à sa position sur cette route marchande.</p>

<!-- Géographie -->
<h2>Géographie</h2>

<p><b>Ses murs d'enceinte</b>, bas mais solides, sont construits en pierre noire extraite des contreforts des Monts Versdvar — tranchant nettement avec le granit clair de Sardam et donnant au bourg, par temps gris, une silhouette particulièrement austère.</p>

<p>La <b>place centrale</b>, assez large pour accueillir six ou sept chariots simultanément, est flanquée de deux tavernes et des grandes écuries du bourg.</p>

<p>Le bourg possède un <b>prieuré</b> atrimiste, ainsi qu'une <b>petite église</b> réputée pour sa chapelle des Voyageurs : les fidèles y laissent des ex-votos avant d'entrer dans la Forêt Rêveuse, et la tradition veut qu'aucun voyageur ayant fait l'offrande prescrite n'y ait jamais péri. Les esprits sceptiques feront remarquer que les voyageurs qui périssent dans la forêt ne reviennent pas démentir la tradition.</p>

