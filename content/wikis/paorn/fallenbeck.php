 <h2>Ressources &amp; services</h2>

<h3>L’auberge du Cheval de bois</h3>
<p><i>Au Cheval de Bois</i> est la seule auberge de Fallenbeck, et son propriétaire, <i>Hüller</i>, ne l’a pas oublié. Ses tarifs sont élevés : chambre à 12 pc, nuit en dortoir à 3 pc, repas 5 pc. Par mauvais temps, la grande salle et souvent pleine. Les voyageurs venant du nord s'y effondrent, trempés, fatigués et silencieux ; ceux qui vont vers le nord s'y attardent un peu plus longtemps que prévu, guettant une éclaircie.</p>

<p>L'établissement peut loger jusqu'à cinquante personnes : un dortoir commun de vingt-quatre couchettes à l'étage, et dix chambres pour deux ou trois personnes. Les draps sont propres et les paillasses honnêtes — Hüller n'est pas négligent, il est simplement indifférent. Il fait ce qu'il faut pour que personne ne se plaigne officiellement, et pas davantage.</p>

<p>La nourriture est correcte et sans surprise : soupe épaisse, pain de seigle, viande bouillie. On mange pour tenir la route. La salle elle-même est grande, mal éclairée, avec des tables longues où les gens s'assoient sans se parler. L'atmosphère est celle d'un endroit de transit : chacun est dans sa fatigue ou dans ses calculs, et personne ne cherche la conversation.</p>

<h3>Les deux tavernes</h3>
<p>Les habitants de Fallenbeck ne fréquentent pas <i>Au Cheval de Bois</i> — ils laissent ça aux étrangers. Pour boire un verre entre gens du village, ils vont aux deux tavernes de la place, où les prix sont raisonnables. On y boit de la bière et du cidre.</p>

<h3>L’alchimiste Henvar</h3>
<p><i>Henvar</i>, l’alchimiste du village, tient boutique près des écuries. Sa réputation technique est solide ; ses tarifs, jugés excessifs par tous (+30 %), sont néanmoins acceptés, Fallenbeck étant le dernier point d'approvisionnement avant plusieurs jours de forêt.</p>