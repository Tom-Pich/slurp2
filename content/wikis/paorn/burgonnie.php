	<!-- Valtarno -->
	<details>
		<summary>
			<h3>La destruction de Valtarno (1183)</h3>
		</summary>

		<p>En 1183, <b>l'Empereur Harvan III</b> ordonna la destruction complète de <b>Valtardam</b> — cité sordolienne du nord de la province, connue sous le nom de Valtarno dans les textes officiels, située à la frontière du Salthar. La ville fut rasée, ses habitants dispersés ou tués, son emplacement maudit par décret impérial.</p>

		<p>La version officielle parle d'une révolte ouverte contre l'autorité impériale, doublée d'une pratique généralisée de la magie noire par une faction locale. La vérité, établie par l'enquête de l'<b>Arcania</b> qui précéda les opérations militaires, était plus grave encore. Valtarno organisait depuis plusieurs décennies un trafic clandestin de métaux précieux et de gemmes entre les mines naines de Geldam et le Salthar. Les négociants de la ville servaient d'intermédiaires entre des artisans nains corrompus et des acheteurs saltharites, court-circuitant à la fois les redevances dues à la Couronne et l'accord de neutralité qui liait la communauté naine à l'Empire. Une fraction du trésor impérial — stocké à Geldam — prenait ainsi le chemin de l'ennemi historique, année après année, sous couvert de commerce ordinaire.</p>

		<p>Ce qui est certain, c’est que la destruction de Valtarno établit un précédent. Depuis lors, aucun seigneur burgon n'a osé contester ouvertement l'autorité impériale par les armes — pas avant la <i>Révolte des Barons</i>, trois siècles plus tard. Les ruines sont encore là, réputées hantées, et portent dans les récits populaires le nom de <i>la Cité maudite</i>.</p>

		<p>La destruction de Valtarno eut une conséquence durable dans les institutions : Harvan III promulgua dans la foulée le premier édit interdisant officiellement les guerres entre nobles burgons. Les « accidents de chasse » entre territoires rivaux continuent de se produire, mais la guerre ouverte entre seigneurs est depuis lors illégale.</p>

	</details>

	<!-- La Guerre des Confins -->
	<details>
		<summary>
			<h3>La Guerre des Confins (1382–1407)</h3>
		</summary>

		<p>La guerre contre le <b>Salthar</b> — connue dans les chroniques impériales sous le nom de <i>Guerre des Confins</i> — fut le conflit le plus long et le plus coûteux de l'histoire de l'Empire, avec vingt-cinq ans d'hostilités ouvertes entre 1382 et 1407.</p>

		<p>Le Salthar, puissance frontalière à l'ouest, disputait depuis des décennies le contrôle des cols des monts du Salthar et des routes commerciales qui les traversaient, notamment dans la zone du <i>Haut Melkiar</i>. Les escarmouches frontalières, chroniques depuis deux siècles, dégénérèrent en guerre totale sous l'impulsion de <b>Grethak le Tranchant</b>, chef de guerre saltharite qui unifia pour la première fois les clans de l'est sous une bannière commune et lança une offensive dans le sud du Verbleau et l'Artaille.</p>

		<p>La <b>nécropole d'Ophilie</b>, à la frontière artaillaise, fut le théâtre de l'une des batailles les plus meurtrières du conflit — des milliers de soldats des deux camps y périrent en moins d'une semaine. Les chroniqueurs rapportent que les combats furent si intenses que les corps n'eurent pas le temps d'être enterrés. C'est sans doute ce qui explique la réputation hantée du lieu depuis lors.</p>

		<p>La guerre se conclut en 1407 par le <b>Traité de Maylie</b>, signé dans le grand port artaillais. Il fixe la frontière, accorde au Salthar un droit de transit limité sur certaines routes marchandes, et impose à l'Empire salthrite une rançon diplomatique symbolique — mais coûteuse. Ce n'est pas une victoire burgonne : c'est un épuisement mutuel.</p>

		<p>La guerre laissa l'Empire burgon saigné à blanc : impôts extraordinaires, nobles appauvris, routes dégradées. Elle fut l'une des causes profondes des tensions qui menèrent, soixante-cinq ans plus tard, à la Révolte des Barons.</p>
	</details>

	<!-- Fordor XXIV -->
	<details>
		<summary>
			<h3>L'avènement de Fordor XXIV (1446)</h3>
		</summary>

		<p>En 1446, <b>Fordor XXIV</b> monte sur le trône impérial dans des circonstances qui alimentent encore les rumeurs. Né <i>Fordor d'Ambrelin</i>, fils d'un noble de province peu influent, il était en théorie très loin dans l'ordre de succession. C'est son ami et conseiller <b>Dolmiov</b>, Tsovranien aux talents de généalogiste réputé, qui découvrit dans les archives d'Ogdapol les titres qui établissaient la filiation. En quelques années, les héritiers plus immédiatement placés que Fordor moururent les uns après les autres — certains de maladie, d'autres dans des accidents de chasse, un par noyade, un dans un incendie. Les contemporains comptèrent. Personne ne dit rien ouvertement.</p>

		<p>Fordor XXIV s'imposa vite comme un souverain à poigne. Petit, trapu, à la longue barbe blanche, en apparence débonnaire — en pratique redoutable. Il concentra le pouvoir, surveilla de près les Assemblées provinciales et multiplia les audits fiscaux dans les provinces, source de revenus pour un trésor impérial encore fragilisé par la Guerre des Confins. En quarante-quatre ans de règne, il a survécu à près d'une vingtaine de tentatives d'assassinat — ce qui dit quelque chose, soit sur le nombre de ses ennemis, soit sur son talent pour les devancer.</p>

		<p>C'est sous son règne que les lois sur la magie furent assouplies : les magiciens ne sont plus obligatoirement des fonctionnaires impériaux assermentés, mais peuvent exercer librement moyennant une patente délivrée par l'Arcania. Une réforme pragmatique, qui maintient la surveillance sans brider une ressource utile.</p>
	</details>

	<!-- La Révolte des Barons -->
	<details>
		<summary>
			<h3>La Révolte des Barons — la Ligue des Sires-Francs (1472–1474)</h3>
		</summary>

		<p>En 1472, une coalition de petits et moyens seigneurs d'Artaille et de Verbleau forma la <b>Ligue des Sires-Francs</b> et refusa collectivement de s'acquitter des impôts extraordinaires levés par Fordor XXIV pour financer la reconstruction des fortifications frontalières. Ils profitèrent également de l'occasion pour réclamer une réduction du pouvoir du Grand Conseil impérial et une plus grande autonomie des Assemblées provinciales.</p>

		<p>Ce n'était pas une armée en campagne mais une rébellion institutionnelle — des seigneurs qui refusaient de payer, retiraient leurs contingents de l'armée impériale et scellaient entre eux des pactes de défense mutuelle. Leur chef de file, le baron <b>Aldram Kesselt de Moreval</b>, était un homme habile, orateur reconnu, qui sut transformer une jacquerie fiscale en programme politique cohérent : <i>l'Empire pour les seigneurs, pas contre eux</i>.</p>

		<p>Fordor répondit par la force et la ruse en égale proportion. Il défit militairement les ligués lors de la <b>bataille de Capria</b> en 1473, en Artaille, brisant l'axe principal de la rébellion. Kesselt de Moreval fut arrêté, jugé et exécuté. Une vingtaine d'autres chefs de la Ligue subirent le même sort ou furent dépouillés de leurs titres. Une centaine de familles nobles furent frappées de lourdes amendes.</p>

		<p>La Sordolia et le prince d'Avrelanche de l'époque — père de Margor — restèrent fidèles à l'Empereur. Cette loyauté leur valut une reconnaissance du trône qui explique en partie l'autonomie relative dont jouit encore aujourd'hui la maison d'Avrelanche.</p>

		<p>La répression de la Ligue laissa l'Empire plus centralisé mais plus méfiant. Fordor XXIV, depuis lors, surveille de près ses nobles — c'est dans ce contexte que s'inscrivent les audits provinciaux réguliers, dont celui qui est en cours en Sordolia en 1490.</p>
	</details>

	<!-- Situation actuelle -->
	<details>
		<summary>
			<h3>L'Empire en 1490 T.A.</h3>
		</summary>

		<p>Seize ans après la répression de la Ligue des Sires-Francs, l'Empire burgon présente une façade de stabilité. Fordor XXIV règne toujours — à soixante-quinze ans passés, il reste actif, méfiant, imprévisible. Aucun héritier n'est clairement désigné. Soixante-cinq parents éloignés vivent sous surveillance dorée au palais d'Olméria, chacun espérant que la nature fera ce que les accidents de chasse ne réussissent plus tout à fait à accomplir.</p>

		<p>Les cicatrices de la rébellion ne sont pas complètement refermées. Certaines familles nobles, appauvries par les amendes et les saisies, entretiennent une rancœur sourde. Le maillage d'audits provinciaux instauré depuis 1475 maintient juridiquement les nobles dans la légalité — mais engendre son lot de ressentiments locaux. En Sordolia comme ailleurs, les nobles regardent les émissaires impériaux avec la même expression : polie, distante, opaque.</p>
	</details>

