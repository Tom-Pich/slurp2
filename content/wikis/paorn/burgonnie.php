<section>
	<h2>Figures et groupes d’influence</h2>

	<!-- L'Empereur -->
	<details>
		<summary>
			<h3>L'Empereur Fordor XXIV</h3>
		</summary>
		<p>Malgré sa stature modeste d'un mètre cinquante-deux et ses quatre-vingt-dix kilos, l'Empereur Fordor XXIV impose le respect à travers tout l'Empire burgon. Reconnaissable à sa longue barbe blanche et ses joues roses qui lui donnent l'apparence trompeuse d'un souverain débonnaire, il est en réalité un stratège politique redoutable.</p>
		<p>Son règne, qui dure depuis quarante-quatre ans, constitue le plus long de l'histoire récente de la Burgonnie, ce qui lui vaut une réputation quasi « d'immortel ». Ayant survécu à de très nombreuses « tentatives d'accidents », Fordor XXIV ne fait confiance qu'à deux personnes : son vieux et fidèle conseiller <i>Dolmiov</i>, un Tsovranien d'une intégrité irréprochable qu'il connut pendant ses études à l'université d'Ogdapol, et maître <i>Hiérolapus</i>, un magicien engagé il y a cinq ans exclusivement pour sa protection.</p>
		<p>Selon les rumeurs, c'est en compulsant les archives de la bibliothèque de la Sagesse antique que Dolmiov découvrit que son ami Fordor, alors simple étudiant connu sous le nom de Fordor d'Ambrelin, était bien placé sur la liste des héritiers de l'empereur Giltur XV. Certains chuchotent que les deux compères auraient méthodiquement éliminé tous les prétendants qui les séparaient du trône. Conscient que soixante-cinq parents plus ou moins éloignés convoitent sa position, l'Empereur assiste désormais personnellement aux réunions du Grand Conseil impérial, après avoir longtemps délégué cette tâche à Dolmiov.</p>
	</details>

