<h1>La Burgonnie</h1>
<p>L’Empire Burgon est l’une des grandes puissances de Paorn, un vaste territoire aux paysages enchanteurs et au climat privilégié. Sous l’apparente splendeur de ses villes et la richesse de ses terres fertiles se cache un système politique complexe, fait d’intrigues permanentes et de luttes de pouvoir. Cet empire morcelé, malgré ses divisions internes, maintient une stabilité relative grâce à des institutions séculaires et une structure sociale bien définie.</p>

<!-- Géographie -->
<section>
	<h2>Géographie</h2>
	<p>L’Empire Burgon bénéficie d’une situation géographique privilégiée. Protégé au nord par la chaîne des Sinistérias et à l’ouest par les monts du Salthar, il dispose sur sa façade est de côtes baignées par des courants chauds qui lui assurent un climat tempéré agréable tout au long de l’année.</p>
	<p>Son territoire est composé de trois grandes provinces :</p>
	<ul>
		<li>La Sordolia au nord</li>
		<li>Le Verbleau au centre</li>
		<li>L’Artaille au sud</li>
	</ul>

	<p>Environ 60% du territoire est couvert de forêts, souvent denses et parfois impénétrables. Le reste se compose principalement de plaines fertiles et de collines verdoyantes, offrant une grande variété de paysages, des forêts profondes du nord aux plaines vallonnées du sud.</p>

	<p>La capitale, Olméria, abrite le palais impérial et constitue le centre politique et administratif de l’empire. </p>

	<p>Les forêts burgonnes recèlent de nombreux dangers, depuis les bandes de brigands jusqu’aux créatures monstrueuses. On y trouve également de nombreux ermites et magiciens reclus, qui préfèrent la solitude des bois à la surveillance des autorités impériales.</p>
</section>

<!-- Habitants -->
<section>
	<h2>Habitants</h2>
	<p>La population de l’Empire Burgon est très majoritairement humaine, mais plusieurs communautés non-humaines y cohabitent pacifiquement. Les Burgons sont généralement décrits comme des gens accueillants et pieux, mais aussi fins politiciens et parfois opportunistes.</p>

	<!-- Répartition raciale -->
	<details>
		<summary>
			<h3>Répartition raciale</h3>
		</summary>
		<ul>
			<li><b>Humains :</b> Ils constituent la majorité de la population et occupent tous les échelons de la société.</li>
			<li><b>Nains :</b> Principalement installés en Sordolia, ils sont respectés pour leurs compétences artisanales.</li>
			<li><b>Elfes :</b> Ils préfèrent les bois du Verbleau où ils ont établi quelques cités importantes comme Thiliandiline et Varilia. En Sordolia, des tribus d’elfes des bois vivent isolées dans les profondes forêts.</li>
			<li><b>Hobbits :</b> Souvent employés comme ouvriers salariés parmi les humains, ils maintiennent également des communautés agraires autonomes, principalement dans le Verbleau et l’Artaille.</li>
		</ul>
	</details>

	<!-- Structure sociale -->
	<details>
		<summary>
			<h3>Structure sociale</h3>
		</summary>
		<p>La société burgonne est hiérarchisée en classes bien définies :</p>
		<ul>
			<li><b>Les courtisans :</b> Parents proches de l’empereur, ils vivent en permanence au palais impérial, sous surveillance étroite.</li>
			<li><b>Les nobles :</b> Ils constituent l’élite de l’empire, cultivés et amateurs d’arts, ils portent des armures ouvragées en signe de statut.</li>
			<li><b>Les bourgeois :</b> Marchands, artisans et ouvriers des villes, ils supportent l’essentiel du poids des taxes mais détiennent un réel pouvoir économique.</li>
			<li><b>Les intellectuels :</b> Savants, artistes et érudits, ils bénéficient d’un statut privilégié malgré des ressources souvent modestes.</li>
			<li><b>Les ouvriers libres et mercenaires :</b> Groupe hétéroclite comprenant rémouleurs, trappeurs, médecins itinérants et autres professions indépendantes.</li>
			<li><b>Les serfs et métayers :</b> Bien que théoriquement dépendants de leurs seigneurs, ils jouissent d’une liberté relative et peuvent même voyager dans l’empire.</li>
		</ul>
	</details>
</section>


<!-- Politique & organisation -->
<section>
	<h2>Politique &amp; organisation</h2>
	<p>L’Empire Burgon est un "empire féodal fédératif" divisé en trois provinces gouvernées par des nobles héréditaires. Chaque province est elle-même fragmentée en duchés, comtés et baronnies dont les seigneurs entretiennent leurs propres armées.</p>

	<!-- Gouvernement -->
	<details>
		<summary>
			<h3>Gouvernement</h3>
		</summary>
		<p>L’empereur actuel, Fordor XXIV, règne depuis quarante-quatre ans, un record de longévité dans un pays où les "accidents" politiques sont monnaie courante. De petite taille mais à la volonté de fer, il a survécu à près de cinq cents tentatives d’assassinat.</p>
		<p>Le pouvoir impérial s’appuie sur plusieurs institutions :</p>
		<ul>
			<li><b>Le Grand Conseil impérial :</b> Composé de vingt-neuf sages choisis par l’empereur parmi les nobles ayant renoncé à leurs titres, il rédige les décrets que l’empereur doit ratifier.</li>
			<li><b>Les Assemblées provinciales :</b> Au nombre de trois (nord, centre, sud), elles appliquent les décisions du Grand Conseil et gèrent les problèmes spécifiques à leur province.</li>
			<li><b>Les guildes et corporations :</b> Elles supervisent tous les domaines d’activité commerciale et artisanale.</li>
			<li><b>Les comices locaux :</b> Assemblées représentatives à l’échelle des villes, bourgs et villages, elles sont ouvertes à toutes les classes sociales.</li>
		</ul>
	</details>

	<!-- Forces de l’ordre -->
	<details>
		<summary>
			<h3>Forces de l’ordre</h3>
		</summary>
		<p>Plusieurs corps assurent le maintien de l’ordre :</p>
		<ul>
			<li><b>Les carmins :</b> Police impériale reconnaissable à ses armures rouge sombre, redoutée pour sa brutalité.</li>
			<li><b>La garde impériale :</b> Corps d’élite de l’armée, reconnaissable à ses armures noir et or, employé pour la défense des intérêts stratégiques.</li>
			<li><b>L’Arcania :</b> Police magique chargée de traquer les magiciens illégaux, divisée en enquêteurs (en uniforme bleu roi et argent) et justiciers (en civil).</li>
			<li><b>L’armée impériale :</b> Composée de soldats professionnels, de contingents fournis par les vassaux et de mercenaires.</li>
		</ul>
	</details>

	<!-- Relations diplomatiques -->
	<details>
		<summary>
			<h3>Relations diplomatiques</h3>
		</summary>
		<p>L’Empire Burgon entretient :</p>
		<ul>
			<li>D’excellentes relations commerciales avec la Lauria, les îles du Tarot et Majistra</li>
			<li>Des relations tendues avec le Salthar, en raison de conflits frontaliers récurrents</li>
			<li>Une "neutralité teintée de méfiance" avec l’Althusia</li>
			<li>Des relations distantes avec la lointaine Tsovranie</li>
		</ul>
	</details>
</section>

<!-- Économie -->
<section>
	<h2>Économie</h2>
	<p>L’Empire Burgon doit sa richesse à ses terres fertiles et à sa stabilité relative. L’agriculture y est florissante, bénéficiant d’un climat favorable et de sols généreux. Les forêts qui couvrent une large partie du territoire fournissent également d’abondantes ressources en bois.</p>
	<p>Le commerce est réglementé par de puissantes guildes et corporations qui supervisent tous les aspects des échanges, de la qualité des produits aux tarifs pratiqués. Cette organisation stricte assure la réputation des marchandises burgonnes au-delà des frontières.</p>
	<p>Les échanges commerciaux sont particulièrement développés avec la Lauria, les îles du Tarot et le continent de Majistra, contribuant significativement à la prospérité de l’empire.</p>
</section>

