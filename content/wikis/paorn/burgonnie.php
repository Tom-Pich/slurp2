<!-- Culture et société -->
<section>
	<h2>Culture et société</h2>

	<!-- Religion -->
	<details>
		<summary>
			<h3>Religion</h3>
		</summary>
		<p>L’Atrimisme est la religion dominante de l’Empire Burgon. Cette foi monothéiste s’est largement imposée dans toutes les couches de la société. Toutefois, certaines branches non orthodoxes existent, ainsi que quelques cultes minoritaires tolérés par les autorités.</p>
		<p>En dehors de l’atrimisme, la pratique de la magie cléricale est autorisée, mais uniquement dans le cadre de cérémonies religieuses approuvées par les comices locaux.</p>
	</details>

	<!-- Arts et éducation -->
	<details>
		<summary>
			<h3>Arts et éducation</h3>
		</summary>
		<p>Les nobles burgons cultivent un goût prononcé pour les arts et la littérature. Beaucoup pratiquent la musique (flûte, épinette, luth) ou s’adonnent à la peinture.</p>
		<p>L’université d’Ogdapol, à Olméria, est renommée pour son enseignement des sciences généalogiques. L’éducation est particulièrement valorisée parmi les élites, et les savants itinérants sont bien accueillis dans tout l’empire.</p>
		<p>La généalogie est considérée comme un art à part entière, et les clercs généalogistes jouissent d’un prestige considérable.</p>
	</details>

	<!-- Magie -->
	<details>
		<summary>
			<h3>Magie</h3>
		</summary>
		<p>Les lois concernant la magie se sont assouplies au cours des dernières décennies. Autrefois, tous les magiciens étaient des fonctionnaires assermentés qui avaient juré fidélité à la Couronne. Aujourd’hui, la pratique de la magie est réglementée mais plus accessible, à condition d’obtenir une patente officielle contresignée par les autorités provinciales et un mage de l’Arcania.</p>
		<p>Les échanges avec Majistra ont contribué à améliorer la perception de la magie, mais les sanctions contre les praticiens illégaux restent sévères. L’usage de la magie "noire" est toujours puni de mort.</p>
	</details>

	<!-- Généalogie -->
	<details>
		<summary>
			<h3>Généalogie</h3>
		</summary>
		<p>Les Burgons accordent une importance capitale à la généalogie. Dès leur plus jeune âge, ils apprennent par cœur leur arbre généalogique jusqu'à la vingtième génération au moins. La profession de clerc généalogiste est l'une des plus respectées et des plus lucratives de l'empire.</p>
		<p>Les services d’un généalogiste peuvent s’avérer utiles pour quiconque souhaite établir des liens potentiels avec la noblesse locale, source possible de richesse et de pouvoir.</p>
	</details>

	<!-- Expressions -->
	<details>
		<summary>
			<h3>Expressions burgonnes</h3>
		</summary>

		<p>Les habitants de l'empire oriental utilisent couramment toutes sortes d'expressions qui peuvent prêter à des erreurs d’interprétation de la part des étrangers. Voici quelques phrases typiques qu'il est bon de connaître.</p>

		<ul>
			<li>« <i>Il est comme un jeune duc</i> » ou « <i>C'est un cœur noble et impétueux</i> » (à prononcer sur un ton suave) : C'est une crapule, il tuerait père et mère pour atteindre son but, il serait bon de lui rabattre son caquet ou de s'en débarrasser.</li>
			<li>« <i>C'est un homme sans ancêtres</i> » (ton réprobateur) : Il ne vaut rien et ne mérite même pas qu'on s'intéresse à lui.</li>
			<li>« <i>Il croit tout ce que disent les prêtres</i> » (ton sérieux) : C'est un homme de bien, digne de confiance.</li>
			<li>« <i>Il vient d'Althusia</i> » (ton moqueur) : Il n'a pas un sou vaillant, c'est un vagabond.</li>
		</ul>
	</details>

	<!-- Duels -->
	 <details>
		<summary>
			<h3>Les duels</h3>
		</summary>

		<p>Le duel traditionnel fait partie intégrante de la culture burgonne. Il existe trois types de duels codifiés pour régler les différends :</p>
		<ul>
			<li><b>Le juste sang :</b>Combat à l'épée ou au fouet jusqu'à la première blessure, pour les affaires mineures.</li>
			<li><b>Le duel « classique » :</b> se termine lorsqu’un adversaire est hors de combat ou déclare forfait. L’usage veut que l’on évite de porter des coups mortels.</li>
			<li><b>Le duel à mort :</b> pour les cas les plus graves.</li>
		</ul>
	 </details>

</section>

<!-- Histoire -->
<section>
	<h2>Histoire</h2>
	<p>L’histoire de l’Empire Burgon est marquée par une succession de luttes de pouvoir et d’intrigues politiques. La tradition de la succession par droit d’aînesse, associée à l’importance accordée à la généalogie, a souvent conduit à des « accidents » opportuns qui ont permis à certaines lignées de s’imposer au détriment d’autres.</p>
	<p>Parmi les événements historiques notables figure la révolte de Valtarno, « la cité maudite », qui fut entièrement rasée par les troupes impériales il y a trois siècles.</p>
	<p>Les guerres entre nobles burgons sont officiellement interdites depuis plus de trois siècles par décret impérial, mais les « accidents de chasse » entre territoires rivaux continuent de se produire régulièrement.</p>
</section>

