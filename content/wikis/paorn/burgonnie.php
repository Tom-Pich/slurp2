	<details>
		<summary>
			<h3>Magie</h3>
		</summary>
		<p>Les lois concernant la magie se sont assouplies au cours des dernières décennies. Autrefois, tous les magiciens étaient des fonctionnaires assermentés qui avaient juré fidélité à la Couronne. Aujourd’hui, la pratique de la magie est réglementée mais plus accessible, à condition d’obtenir une patente officielle de l’Arcania.</p>
		<p>Les sanctions contre les praticiens illégaux restent sévères. L’usage de la magie noire est toujours puni de mort.</p>
