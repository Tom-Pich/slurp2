<section>
	<h2>Histoire</h2>

	<p>Les dates sont donnée selon le <a href="wiki/paorn/calendrier-burgon">calendrier burgon</a>.</p>

	<!-- Les confédérations primitives -->
	<details>
		<summary>
			<h3>Les confédérations primitives (avant 795 T.A.)</h3>
		</summary>

		<p>Avant la fondation de l'Empire, le territoire que l'on appelle aujourd'hui la Burgonnie était partagé entre deux grandes confédérations de cités-états et de petits royaumes : le <b>Verbleau</b> au centre et l’<b>Artaille</b> au sud. Ces deux entités n'avaient ni frontières fixes ni gouvernement centralisé — elles étaient des coalitions d'intérêts, remodelées à chaque génération par les guerres de succession, les alliances matrimoniales et les querelles de préséance.</p>

		<p>La religion atrimiste, déjà ancienne, jouait un rôle de ciment moral. Le prophète Eldar était mort depuis sept siècles, mais ses enseignements, rassemblés dans <i>La Voie de la Vertu</i>, étaient le patrimoine commun des deux confédérations. La ville de <b>Stomilie</b>, où Eldar était né et où reposait son tombeau, était déjà un lieu de pèlerinage respecté des deux clans — l'un des rares points de convergence entre des peuples souvent en guerre.</p>

		<p>Cette période est marquée par des conflits incessants mais rarement dévastateurs — des guerres de frontière, des escarmouches de seigneurs voisins, des sièges de villes fortifiées. Pas d'efforts unitaires, pas de volonté commune. Ce qui empêchait toute expansion vers le nord — la Sordolia restait une terre de petits royaumes humains indépendants — et vers le sud, où l'Althusia constituait une menace diffuse.</p>
	</details>

	<!-- Fondation de l'Empire -->
	<details>
		<summary>
			<h3>La fondation de l'Empire (795)</h3>
		</summary>

		<p>La fondation de l'Empire burgon est datée officiellement du <b>14 octobre 795 T.A.</b> — le jour où l'Assemblée des seigneurs du Verbleau et de l'Artaille, réunis à Stomilie, ratifièrent le Pacte d'Unité et reconnurent comme premier Empereur <b>Aldric I<sup>er</sup> de Fornay</b>, roi du Verbleau et chef de guerre incontesté de sa génération.</p>

		<p>Aldric, connu sous le surnom de <i>l'Unificateur</i>, avait passé vingt ans à consolider son pouvoir dans le Verbleau par une combinaison de victoires militaires et d'alliances matrimoniales. Sa décision de ne pas conquérir l'Artaille mais de la rallier par la diplomatie, en offrant à ses nobles une représentation égale au sein d'un conseil commun, fut le geste fondateur qui rendit l'empire possible. L'Atrimisme fut proclamé religion d'état — un choix naturel, Stomilie se trouvant en territoire artaillais, et la légitimité spirituelle qu'offrait la ville sainte étant un atout politique considérable.</p>

		<p>Aldric mourut treize ans après la fondation, sans avoir eu le temps de fixer pleinement ses institutions. Ses successeurs immédiats — <b>Aldric II le Hardi</b>, puis <b>Aldric III le Sévère</b> — consacrèrent leurs règnes à étouffer les révoltes des seigneurs qui n'avaient jamais vraiment accepté la suprématie impériale. Ce n'est qu'avec <b>Malderen I<sup>er</sup> le Sage</b>, sixième Empereur, que l'empire prit sa forme administrative stable : le Grand Conseil impérial, les Assemblées provinciales, la structure des guildes et des comices locaux.</p>

		<p>C'est également sous Malderen I<sup>er</sup> que fut créée en 935 l'<b>Arcania</b>, la police magique, chargée de surveiller l'usage de la magie dans l'empire. La méfiance atrimiste envers la magie trouvait ainsi une traduction institutionnelle — les magiciens, trop longtemps libres de leurs actes, étaient désormais répertoriés, assermentés et soumis à la loi commune.</p>
	</details>

