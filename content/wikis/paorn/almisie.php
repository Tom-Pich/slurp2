<img src="/assets/img_paorn/blason-almisie.png" width="70px" class="float-left">

<p>Almisie est une ville réputée pour son artisanat, en particulier pour ses cristalleries. Les artisans d'Almisie sont célèbres pour leur habileté à façonner des objets en cristal d'une finesse et d'une beauté inégalées. Dans les environs de la ville sont produits des vins réputés dans tout Paorn, dont les vignobles s'étendent à perte de vue sur les collines environnantes. Malgré sa relative richesse, Almisie ne possède pas de murs d'enceinte, mais ses troupes sont largement suffisantes pour assurer sa sécurité contre d'éventuels pillards.</p>

<p>
	Elle compte environ 20.000 habitants, soit presque autant que la capitale, Imégie. N’étant pas ceinte par des murailles, la ville s’étend sur une assez grande surface (150 ha). Les maisons d'Almisie sont principalement construites en pierre locale, avec des toits de tuiles rouges qui brillent sous le soleil. Dans la partie la plus ancienne de la ville, les rues pavées sont bordées de fontaines et de statues, témoignant de la prospérité de la cité.<br>
	Almisie connaît, depuis quelques décennies, une forte croissance démographique. Il en résulte que les parties les plus extérieures de la ville ont été construites dans une certaine anarchie. Les rues ne sont pas pavées, et les maisons, généralement d’assez petite taille, ont été baties à la va-vite, avec assez peu de soucis architecturaux. Ces zones, appelées les «&nbsp;<i>bans</i>&nbsp;», donnent au voyageur nouvellement arrivé dans la ville une impression peu flatteuse.
</p>

<p>Almisie est située dans une région vallonnée, entourée de champs, de vignes et de bois épars. Malgré l’absence de cours d’eau qui la traverse, la ville jouit d’un sous-sol riche en eau et possède de nombreux puits et fontaines, dont certaines sont ornées de sculptures en cristal. Cette cité est un carrefour commercial, passage obligé de nombreuses routes marchandes. Les marchés d'Almisie sont animés et colorés, offrant une variété de produits allant des épices exotiques aux étoffes précieuses.</p>

<p>Il y fait une chaleur écrasante en été, bien que des pluies régulières permettent aux cultures et aux bêtes de prospérer. Les habitants d'Almisie célèbrent chaque année la Fête des Vignes, une grande fête où le vin coule à flots et où des spectacles de musique et de danse animent les rues. La ville est également connue pour ses spécialités culinaires, comme le ragoût de lapin aux herbes des collines et les tartes aux fruits des bois.</p>

<h3>PNJ, groupes & organisations</h3>

<h4>Edar Valmor, seigneur d’Almisie, cousin du prince Sémilis d’Imégie</h4>
<div class="wiki-right-element flow">
	<figure>
		<img src="/assets/img_paorn/pnj-edar-valmor.jpg" />
		<figcaption>Edar Valmor, seigneur d’Almisie</figcaption>
	</figure>
</div>

<p>Edar Valmor est un homme d'une quarantaine d'années, de stature imposante et d'allure noble. Ses cheveux noirs, parsemés de quelques mèches grises, sont toujours soigneusement coiffés. Ses yeux perçants, d'un bleu profond, reflètent une intelligence vive et une détermination sans faille.</p>

<p>Edar est un homme complexe, à la fois charismatique et calculateur. Il est connu pour son esprit stratégique et sa capacité à naviguer habilement dans les eaux troubles de la politique. Bien qu'il soit respecté par ses sujets pour sa gestion efficace de la ville et son soutien à l'artisanat local, il est également craint pour sa détermination implacable à protéger les intérêts d'Almisie, même au détriment de ses relations familiales.</p>

<p>La relation entre Edar et son cousin, le Prince d'Imégie, est marquée par une rivalité sous-jacente. Bien qu'ils partagent un lien de sang, leurs ambitions respectives les ont souvent mis en opposition. Edar n'hésite pas à engager des guerres commerciales avec Imégie, cherchant à renforcer la position économique d'Almisie. Cependant, il veille toujours à respecter les conventions diplomatiques, évitant ainsi que les tensions ne dégénèrent en conflit ouvert.</p>

<p>Sous la direction d'Edar, Almisie a prospéré. Il a mis en place des politiques favorisant le commerce et l'artisanat, attirant des artisans et des marchands de toute la région. Il a également investi dans les infrastructures de la ville, améliorant les routes, les marchés et les systèmes d’approvisionnement en eau. Sa vision pour Almisie est celle d'une cité indépendante et florissante, capable de rivaliser avec la capitale.</p>

<p>Edar est marié à Dame Séraphine, une femme d'une grande beauté et d'une intelligence égale à la sienne. Ensemble, ils ont deux enfants, un fils nommé Aric et une fille nommée Elara.</p>

