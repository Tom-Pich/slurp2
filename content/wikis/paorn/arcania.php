<h2>Législation, mages &amp; magie</h2>

<p>En Burgonnie, pour avoir le droit d’exercer légalement la magie, un mage doit se déclarer auprès de l’Arcania. Après une <i>Investigation mentale</i>, celui-ci reçoit alors une <i>Suggestion</i> permanente l’enjoignant à « respecter la loi » (variante du sort <i>Suggestion</i> de niveau IV). Il reçoit également un anneau (qu’il doit porter) qui constitue le signe visible de son caractère « officiel ». Il est bien sûr totalement illégale de lancer une <i>Dissipation de la magie</i> sur cette <i>Suggestion</i>.</p>

<p>Un certain nombre de sorts (et de potions) sont illégales. Il s’agit notamment de tous les sorts qui ne peuvent être que « maléfiques » – <i>Invocation de démon</i>, <i>Animation des morts</i> en sont deux exemples.</p>

<h2>Mages gris</h2>

<p>Les mages « gris », ou mages clandestins, sont personnes qui utilisent la magie sans s’être déclarés à l’Arcania. Ils ne sont donc pas sous l’effet de la <i>Didascalie</i> les obligeant à être honnête.</p>
<p>Ils sont pourchassés par l’Arcania et les peines qu’ils encourent dépendent de leurs actions passées (qui sont révélées grâce à <i>Investigation mentale</i>). Dans tous les cas, la peine est sévère et un mage peut assez facilement être condamné à mort pour ce crime s’il a mené des actions illégales graves.</p>
<p>Cependant, les mages gris restent assez nombreux. Ils vivent dans de petits villages ou bien retirés du monde. Tant qu’ils ne font pas parler d’eux, l’Arcania n’a pas le temps de s’en occuper.</p>

<p>Il existe également un certain nombre de sorts permettant de fausser les informations obtenues par un mage de l’Arcania grâce à des sorts tels que <i>Lecture d’aura</i> ou <i>Investigation mentale</i>. Mais ces sorts sont peu répandus et leur utilisation est considéré comme un crime punissable de mort sir l’Arcania s’en aperçoit.</p>