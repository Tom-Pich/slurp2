</figure>

<p>Séparée de la côte d’Artaille par une large passe aux courants capricieux, la Lauria est un territoire farouchement indépendant. Ses habitants ont développé au fil des siècles une culture originale, tournée vers l'artisanat, l'élevage et la méditation.</p>

<h3>Géographie</h3>

<p>De hautes montagnes occupent les deux tiers de cette île aride. La plus haute est un volcan&nbsp;: le mont Ténéphas. Exception faite des tempêtes qui s’abattent régulièrement sur sa côte orientale, les pluies sont rares, du fait de courants marins chauds et de vents venus du sud qui soufflent toute l'année. La Lauria est couverte de grands pâturages à l'herbe jaunie et de forêts de résineux rabougris. Les villes se sont développées dans sa partie occidentale, pluspropice à l'élevage et à l'agriculture.</p>

<h3>Mikalas (ville)</h3>
<p>Port principal de la Lauria.</p>
<p>Maisons de pierre calcaire aux toits de tuiles, basse (un niveau en général, quelques unes à deux niveaux). Ambiance calme et reposante. Les habitants parlent le commun et un patois incompréhensible pour les PJ. Nombreuses églises de pierres claires aux clochers élancés.</p>
<p>Marchandises circulant sur le port : jarga (liqueur de figue distillée à Draki), toiles et tissus (laine et lain), ébénisterie.</p>

<h4>PNJ, groupes &amp; organisations</h4>
<p><b>Mage Rodek&nbsp;:</b> très vieux (80 ans ?) et frêle. Plongé dans ses recherches sur les changements de plans. Rend quelquefois service pour de l’argent. Habite près de l’église de Saint-Madis, une petite maison mal entretenue. Assisté par un serviteur presque aussi vieux et totalement dévoué. Collèges maîtrisés : Seuils, Métamagie, Connaissance, Emprise mentale (un peu), Communication &amp; Empathie</p>
<p><b>Albon, grand-prêtre de Mikalas&nbsp;:</b> vieil homme sec, dur et sinistre. Condescendant envers les non-humains.</p>
<p><b>Alchimiste Keranis&nbsp;:</b> cheveux auburn et yeux bleux, robe bleue, possède une petite échoppe sur une place (place des Insoumis) où poussent quelques pins, où des vieux sont assis sur des bancs, des chiens dorment à l’ombre et des colombes se baignent sur la fontaine centrale.</p>
<p><b>Alchimiste Dorkos&nbsp;:</b> cheveux long et roux, yeux gris, robe sombre, plus excentré, possède une échoppe plus riche. À proximité du fort Waltin, seul bâtiment en pierre sombre (haute muraille – 10 m), où se trouve la garnison de la ville.</p>

