<!-- Politique & organisation -->
<section>
	<h2>Politique &amp; organisation</h2>
	<p>Les institutions politiques de la Lauria sont assez originales</p>

	<p>Les Lauriens sont convaincus que la jeunesse est la période de la vie où l’homme a le plus de chances de réaliser ses idéaux. L’île est donc gouvernée par le <i>Septar</i>, une petite assemblée composée de sept jeunes gens (dont aucun n’a plus de trente ans) élus par les cités.</p>

	<p>Mais les Lauriens savent également que l’expérience à son importance, c’est pourquoi, avant de pouvoir être appliquées, toutes les décisions prises par le <i>Septar</i> doivent être discutées par le <i>Grand Conseil des Aînés</i> auquel appartiennent de plein droit tous les habitants âgés de plus de soixante ans.</p>

	<p>L’île ne possède pas de véritable armée permanente, mais tous ses habitants adultes (femmes comprises) doivent passer un trimestre tous les trois ans au sein des <i>Protecteurs</i>, des troupes chargées d’assurer aussi bien le maintien de l’ordre que la défense du pays.</p>
</section>

