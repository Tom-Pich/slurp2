<p>Séparée de la côte d’Artaille par une large passe aux courants capricieux, la Lauria est un territoire farouchement indépendant. Ses habitants ont développé au fil des siècles une culture originale, tournée vers l’artisanat, l’élevage et la méditation.</p>

<!-- Géographie -->
<section>
	<h2>Géographie</h2>

	<p>La Lauria a une superficie d’environ 64 000 km², et un pourtour long de 1800 km. Elle compte un peu plus de 350.000 habitants.</p>

	<p>Par ses paysages et son climat, la Lauria pourrait rappeler la Sardaigne ou les îles grecques. Exception faite des tempêtes qui s’abattent régulièrement sur sa côte orientale, les pluies sont rares, du fait de courants marins chauds et de vents venus du sud qui soufflent toute l’année. </p>

	<p>La Lauria est couverte de grands pâturages à l’herbe jaunie, de forêts de résineux rabougris et de buissons vivaces, souvent épineux.</p>

	<p>De hautes montagnes inhabitées, appelées les <a href="/wiki/paorn/pasganon"><i>Pasganon</i></a>, occupent les deux tiers de cette île aride. Dans ces montagnes se trouvent de nombreuses ruines très anciennes.</p>

	<p>Les villes se sont développées dans sa partie occidentale, plus propice à l’élevage et à l’agriculture.</p>

