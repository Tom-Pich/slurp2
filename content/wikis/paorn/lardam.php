<p>Grande ville portuaire et capitale économique de la Sordolia. C’est la ville la plus peuplée de la province.</p>

<p><b>Le duché de Lardam</b> est le fief le plus riche et le plus peuplé de Sordolia, tourné vers la mer et le commerce maritime. Son duc, <b>Lancmon Hareld</b>, est un homme d’affaires autant qu’un seigneur : il entretient des liens étroits avec les plus riches marchands de fourrures de la région et avec les armateurs de la côte sud. La baronnie de Meck, au nord de Lardam, relève de ce duché en tant que vassal direct du duc.</p>

