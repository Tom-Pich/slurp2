	<!-- Portes de la ville -->
	<details>
		<summary>
			<h3>Portes de la ville</h3>
		</summary>
		<p><b>La Grande Porte :</b> porte principale, située au nord-ouest de la ville, que l’on trouve en venant du reste du duché.</p>
		<p><b>La Porte des Mineurs :</b> au nord-est de la ville. Principalement emprûntée par les mineurs et les pêcheurs habitants dans les hameaux au nord de la ville.</p>
		<p><b>La Vieille Porte :</b> aussi appelée la <i>Porte de la vieille ville</i> sépare la vieille ville de la ville basse. Elle reste en principe ouverte de jour comme de nuit et est constamment surveillée par deux gardes.</p>
	</details>

