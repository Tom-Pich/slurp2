<!-- Géographie -->
<section>

	<h2>Géographie</h2>

	<p>Imégie est la ville la plus peuplée de l’Artaille, avec 25.000 habitants.</p>

	<p>Elle s’étend sur une superficie de 175 ha (2,6 km dans sa plus grande longueur, de la porte de Mineurs au nord-est jusqu'aux murailles de St-Almer au sud-ouest). La citée est divisée en deux partie : la vieille ville, ou ville haute, au sud-ouest, et la ville basse. Ces deux parties sont séparées par une fortification intérieure et communiquent par la <i>Vieille Porte</i>.</p>

	<p>La <i>Campua</i>, rivière prenant sa source dans les monts Hizrak, au nord, traverser la ville dans une gorge étroite.</p>

	<!-- Ville haute et ville basse -->
	<details>
		<summary>
			<h3>Ville haute et ville basse</h3>
		</summary>
		<p>Imégie est divisée en deux parties : la vieille ville et la ville basse.</p>
		<p>La vieille ville (ou ville haute) est ceinte par une muraille intérieure. Elle se décompose en quatre quartiers : la <i>Citadelle</i>, le <i>quartier des Consuls</i> juste après (en rouge), <i>Bellevue</i> (en vert), et enfin <i>Saint-Almer</i> à l'extrême sud-ouest (en jaune). La vieille ville a une superficie de 60 ha environ, et compte 8000 habitants. C’est la partie la plus riche de la ville et la sécurité y est particulièrement renforcée.</p>
		<p>La ville basse, correspondant à peu près à la moitié nord-est de la ville, comprend le port et la place du marché, où se trouve la cathédrale</p>
	</details>

