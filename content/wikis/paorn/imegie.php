			</li>
			<li>
				<b>Les Andars :</b> quartier pauvre. On y trouve le port des pêcheurs, qui n'est en fait qu'une plage de galets. 12 ha.<br>
				La plupart des maisons ne comptent qu’un niveau. Elles sont défraîchies. La plupart des rues ne sont pas pavées, et celles qui le sont ne sont pas en bon état. Le quartier n’est pas sûr la nuit.
			</li>
			<li><b>Martilès :</b> quartier où se trouve la <i>Porte des Mineurs</i>. C’est un quartier pauvre où l’on trouve beaucoup d’étrangers venus travailler dans les mines. 13 ha.</li>
			<li><b>Hobbitbourg :</b> habité exclusivement par des hobbits, il se trouve sur les flancs de la montagne au Nord de la ville. C'est un quartier pauvre, et la majeure partie des habitations sont à moitié creusées dans la montagne. 13 ha pour 1800 habitants.</li>
		</ul>
